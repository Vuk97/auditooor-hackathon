#!/usr/bin/env python3
"""
skill-issues-rollup.py — auto-generated rollup of SKILL_ISSUES.md.

Parses each `### Issue N:` section + its `- **Status:**` line, buckets by
status keyword, and writes `docs/SKILL_ISSUES_STATUS.md` with:
  - Total count + per-bucket breakdown + percentages
  - Per-phase closure count (Status lines containing "Phase 1" ... "Phase 9")
  - Table of all OPEN issues (# / title / 1-line problem)
  - "Recently closed" section listing issues with Status containing "PR #84"

Usage: python3 tools/skill-issues-rollup.py
"""
from __future__ import annotations

import re
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
SRC = ROOT / "SKILL_ISSUES.md"
OUT = ROOT / "docs" / "SKILL_ISSUES_STATUS.md"

OPEN_KEYWORDS = ("open", "pending", "in progress", "deferred", "partial",
                 "drill-pending", "policy codified", "lesson codified",
                 "needs-verify", "engagement complete", "logged")
DONE_KEYWORDS = ("done", "fixed", "closed", "✅", "superseded")

ISSUE_RE = re.compile(r"^###\s+Issue\s+(\d+)\b[^:—-]*\s*[:—-]\s*(.*)$")
STATUS_RE = re.compile(r"^- \*\*Status:\*\*\s*(.*)$")
FRICTION_RE = re.compile(r"^- \*\*Friction:\*\*\s*(.*)$")
PROBLEM_RE = re.compile(r"^- \*\*Problem:\*\*\s*(.*)$")


def classify(status_body: str) -> str:
    if not status_body:
        return "UNKNOWN"
    s = status_body.lower()
    # Check DONE first — a status like "DONE, partial follow-up" should count DONE.
    for kw in DONE_KEYWORDS:
        if kw in s:
            return "DONE"
    for kw in OPEN_KEYWORDS:
        if kw in s:
            return "OPEN"
    return "UNKNOWN"


def classify_issue(iss: dict) -> str:
    # Primary signal: the Status line.
    verdict = classify(iss["status"])
    if verdict != "UNKNOWN":
        return verdict
    # Fallback: some entries are "STATUS UPDATE" shells where the closure verdict
    # is embedded in the title (e.g. "STATUS UPDATE: CLOSED in R67c"). Give the
    # title one pass so those aren't lost as UNKNOWN.
    title_verdict = classify(iss["title"])
    return title_verdict


def parse_issues(text: str) -> list[dict]:
    raw: list[dict] = []
    current: dict | None = None
    for line in text.splitlines():
        m = ISSUE_RE.match(line)
        if m:
            if current:
                raw.append(current)
            current = {
                "num": int(m.group(1)),
                "title": m.group(2).strip(),
                "status": "",
                "problem": "",
            }
            continue
        if current is None:
            continue
        ms = STATUS_RE.match(line)
        if ms and not current["status"]:
            current["status"] = ms.group(1).strip()
            continue
        if not current["problem"]:
            mf = FRICTION_RE.match(line) or PROBLEM_RE.match(line)
            if mf:
                current["problem"] = mf.group(1).strip()
    if current:
        raw.append(current)

    # Dedupe by issue number. When the same # appears twice (e.g. "Issue 25
    # (original body)" followed by a refreshed entry, or "Issue 168 — STATUS
    # UPDATE"), prefer the entry whose classified status is DONE, then the
    # latest occurrence. This keeps the rollup aligned with the live state.
    by_num: dict[int, dict] = {}
    for iss in raw:
        n = iss["num"]
        prev = by_num.get(n)
        if prev is None:
            by_num[n] = iss
            continue
        prev_done = classify_issue(prev) == "DONE"
        this_done = classify_issue(iss) == "DONE"
        if this_done and not prev_done:
            by_num[n] = iss
        elif prev_done and not this_done:
            continue
        else:
            # Same classification — take the later one (iss).
            by_num[n] = iss
    return [by_num[k] for k in sorted(by_num)]


def trim(s: str, n: int = 120) -> str:
    s = s.replace("|", "\\|").strip()
    if len(s) > n:
        s = s[: n - 1].rstrip() + "…"
    return s


def render(issues: list[dict]) -> str:
    total = len(issues)
    buckets = {"OPEN": [], "DONE": [], "UNKNOWN": []}
    for iss in issues:
        buckets[classify_issue(iss)].append(iss)

    def pct(n: int) -> str:
        return f"{(100.0 * n / total):.1f}%" if total else "0.0%"

    lines: list[str] = []
    lines.append("# Skill Issues Status Rollup")
    lines.append("")
    lines.append("_Auto-generated by `tools/skill-issues-rollup.py` from `SKILL_ISSUES.md`. Do not hand-edit._")
    lines.append("")
    lines.append("## Summary")
    lines.append("")
    lines.append(f"- **Total issues:** {total}")
    lines.append(f"- **DONE:** {len(buckets['DONE'])} ({pct(len(buckets['DONE']))})")
    lines.append(f"- **OPEN:** {len(buckets['OPEN'])} ({pct(len(buckets['OPEN']))})")
    lines.append(f"- **UNKNOWN:** {len(buckets['UNKNOWN'])} ({pct(len(buckets['UNKNOWN']))})")
    lines.append("")

    # Per-phase closure count
    lines.append("## Per-phase closure count")
    lines.append("")
    lines.append("| Phase | Closures |")
    lines.append("|-------|----------|")
    for n in range(1, 10):
        pat = re.compile(rf"\bPhase {n}\b")
        c = sum(1 for iss in issues if pat.search(iss["status"] or ""))
        lines.append(f"| Phase {n} | {c} |")
    lines.append("")

    # OPEN table
    open_list = sorted(buckets["OPEN"], key=lambda i: i["num"])
    lines.append(f"## Open issues ({len(open_list)})")
    lines.append("")
    if open_list:
        lines.append("| # | Title | Problem |")
        lines.append("|---|-------|---------|")
        for iss in open_list:
            lines.append(
                f"| {iss['num']} | {trim(iss['title'], 80)} | {trim(iss['problem'], 160)} |"
            )
    else:
        lines.append("_None._")
    lines.append("")

    # Recently closed (PR #84)
    recent = [iss for iss in issues if "PR #84" in (iss["status"] or "")]
    recent.sort(key=lambda i: i["num"])
    lines.append(f"## Recently closed (PR #84) — {len(recent)}")
    lines.append("")
    if recent:
        lines.append("| # | Title |")
        lines.append("|---|-------|")
        for iss in recent:
            lines.append(f"| {iss['num']} | {trim(iss['title'], 140)} |")
    else:
        lines.append("_None._")
    lines.append("")

    # UNKNOWN tail (tiny sanity block — helps diagnose parser gaps)
    unk = sorted(buckets["UNKNOWN"], key=lambda i: i["num"])
    if unk:
        lines.append(f"## Unclassified ({len(unk)})")
        lines.append("")
        lines.append("| # | Title | Raw status |")
        lines.append("|---|-------|------------|")
        for iss in unk:
            lines.append(
                f"| {iss['num']} | {trim(iss['title'], 80)} | {trim(iss['status'] or '(missing)', 80)} |"
            )
        lines.append("")

    return "\n".join(lines) + "\n"


def main() -> int:
    if not SRC.exists():
        print(f"error: {SRC} not found", file=sys.stderr)
        return 1
    text = SRC.read_text(encoding="utf-8")
    issues = parse_issues(text)
    OUT.parent.mkdir(parents=True, exist_ok=True)
    OUT.write_text(render(issues), encoding="utf-8")
    buckets = {"OPEN": 0, "DONE": 0, "UNKNOWN": 0}
    for iss in issues:
        buckets[classify_issue(iss)] += 1
    print(
        f"{len(issues)} total, {buckets['DONE']} DONE, "
        f"{buckets['OPEN']} OPEN, {buckets['UNKNOWN']} UNKNOWN"
    )
    print(f"wrote {OUT}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
