def _now_iso() -> str:
    return datetime.now(timezone.utc).isoformat()


def render_probe_shell(
    candidate_id: str,
    target_repo: str,
    app_chain: str,
    claim_text: str,
    validator_count: int,
    go_pkg: str,
) -> str:
    """Render the Go probe shell source text."""
    return _GO_TEMPLATE.format(
        tool=TOOL,
        ts=_now_iso(),
        schema=SCHEMA,
        candidate_id=candidate_id,
        target_repo=target_repo,
        app_chain=app_chain,
        claim_text=claim_text,
        validator_count=validator_count,
        pkg=go_pkg,
        runtime_event_schema="auditooor.cosmos_production_harness_runtime_event.v1",
    )


def emit_manifest(
    *,
    candidate_id: str,
    target_repo: str,
    app_chain: str,
    claim_text: str,
    validator_count: int,
    go_file: str,
) -> dict:
    """Return the JSON manifest for the emitted probe shell."""
    return {
        "schema": SCHEMA,
        "tool": TOOL,
        "emitted_at": _now_iso(),
        "candidate_id": candidate_id,
        "target_repo": target_repo,
        "app_chain": app_chain,
        "claim_text": claim_text,
        "validator_count": validator_count,
        "go_harness_file": go_file,
        "production_profile_satisfied": {
            "real_db_backend": True,
            "finalize_block_commit": True,
            "restart_behavior": True,
            "multi_validator": True,
            "no_reflection": True,
            "transcript_capture": True,
        },
        "rule_obligations": {
            "rule_18": "node-level surface via FinalizeBlock+Commit",
            "rule_19": "real ABCI surface (not direct keeper call)",
            "rule_22": "restart probe via close+reopen same data dir",
            "rule_30_a": "GoLevelDB (never MemDB)",
            "rule_30_b": "no timing shims around DB primitives",
            "rule_30_c": "no reflection/unsafe writes",
            "rule_30_d": f">=2 validators ({validator_count})",
        },
        "nbq": ["NBQ-007", "NBQ-009", "NBQ-010"],
        "lane": "Lane-11-Go-Cosmos-DLT-Proof-Packaging",
        "boundary": (
            "This is a SCAFFOLD, not runtime proof. "
            "Replace TODO stubs with project-specific app construction "
            "and impact assertions before claiming production-grade evidence."
        ),
    }


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__.splitlines()[1].strip())
    ap.add_argument("--workspace", type=Path, required=True)
    ap.add_argument("--poc-dir", type=Path, required=True)
    ap.add_argument("--candidate-id", default="CANDIDATE-001")
    ap.add_argument("--target-repo", default="dydxprotocol/v4-chain")
    ap.add_argument("--app-chain", default="dydx")
    ap.add_argument("--claim-text", default="network-level AppHash divergence across validators")
    ap.add_argument("--validator-count", type=int, default=4)
    ap.add_argument("--go-pkg", default="harness")
    ap.add_argument("--out", type=Path, default=None,
                    help="Output .go file path (default: <poc-dir>/multivalidator_probe_test.go)")
    ap.add_argument("--manifest-out", type=Path, default=None,
                    help="Output manifest JSON path (default: <poc-dir>/multivalidator_probe_manifest.json)")
    ap.add_argument("--json", action="store_true", help="Print manifest JSON to stdout")
    args = ap.parse_args()

    if args.validator_count < 2:
        print(
            f"[{TOOL}] ERROR: --validator-count must be >= 2 for R30(d) compliance "
            f"(got {args.validator_count})",
            file=sys.stderr,
        )
        return 2

    poc_dir = args.poc_dir.resolve()
    poc_dir.mkdir(parents=True, exist_ok=True)

    out_path = args.out or (poc_dir / "multivalidator_probe_test.go")
    out_path = out_path.resolve()

    manifest_path = args.manifest_out or (poc_dir / "multivalidator_probe_manifest.json")
    manifest_path = manifest_path.resolve()

    go_src = render_probe_shell(
        candidate_id=args.candidate_id,
        target_repo=args.target_repo,
        app_chain=args.app_chain,
        claim_text=args.claim_text,
        validator_count=args.validator_count,
        go_pkg=args.go_pkg,
    )
    out_path.write_text(go_src, encoding="utf-8")

    manifest = emit_manifest(
        candidate_id=args.candidate_id,
        target_repo=args.target_repo,
        app_chain=args.app_chain,
        claim_text=args.claim_text,
        validator_count=args.validator_count,
        go_file=str(out_path),
    )
    manifest_path.write_text(json.dumps(manifest, indent=2) + "\n", encoding="utf-8")

    if args.json:
        print(json.dumps(manifest, indent=2))
    else:
        print(f"[{TOOL}] probe shell: {out_path}")
        print(f"[{TOOL}] manifest:    {manifest_path}")
        print(f"[{TOOL}] validators:  {args.validator_count}")
        print(f"[{TOOL}] ok: true")
    return 0


if __name__ == "__main__":
    sys.exit(main())
