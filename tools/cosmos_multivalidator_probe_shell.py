"""Cosmos multi-validator proof-shell generator (Lane 11 NBQ-007/NBQ-009/NBQ-010).

Generates a self-contained multi-validator probe shell for Cosmos/dYdX app-chain
findings that require network-level evidence (Rule 18 / Rule 30 clause d).

This tool complements ``cosmos_dynamic_harness_scaffold.py`` (which covers the
single-validator case: real GoLevelDB backend + FinalizeBlock/Commit + restart).
It produces a SEPARATE Go test file for the multi-validator variant, satisfying
Rule 30 clause (d) requirements:

  "Multi-validator demonstration for network-level claims: any network-level
   liveness, consensus halt, validator halt, AppHash divergence, or chain-halt
   claim needs >=2 validators or equivalent subprocess/binary nodes."

Shell covers:
  1. Real GoLevelDB backend (no MemDB) -- per R30(a)
  2. app-level message path via simapp.Setup or equivalent -- per R19
  3. block execution path via FinalizeBlock + Commit -- per R19
  4. restart probe (close+reopen from same data dir) -- per R22
  5. multi-validator loop (N validators, configurable) -- per R30(d)
  6. no reflection / unsafe / private-state surgery -- per R30(c)
  7. transcript capture (AUDITOOOR_COSMOS_HARNESS_EVENT lines) -- per R30 output contract

Exit codes:
  0 - probe shell emitted
  2 - input validation error

Usage:
    python3 tools/cosmos_multivalidator_probe_shell.py \\
      --workspace /path/to/workspace \\
      --poc-dir /path/to/poc_tests/candidate \\
      --candidate-id CANDIDATE-001 \\
      --target-repo dydxprotocol/v4-chain \\
      --app-chain dydx \\
      --claim-text "AppHash divergence across validators" \\
      --validator-count 4 \\
      --out /path/to/poc_tests/candidate/multivalidator_probe_test.go
"""
