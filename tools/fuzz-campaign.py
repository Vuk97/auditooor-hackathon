#!/usr/bin/env python3
"""fuzz-campaign.py — V5 PR 4 fuzz-campaign wrapper (stdlib-only).

Per docs/ROADMAP_10_OF_10_V5_CAMPAIGNS.md Section 4. Implements a bounded
campaign farm that ORCHESTRATES the existing fuzz/deep tooling rather than
adding a new engine. This is Codex's hard rule: "First inventory existing
fuzz/deep tools before adding new engines."

Existing tools the wrapper orchestrates (do NOT duplicate):

  - tools/fuzz-runner.sh         bounded medusa|echidna|auto runner with
                                 timeout, manifest, counterexample capture
                                 (PR 107 / capv3-iter1-T1).
  - forge test --invariant       Forge invariant/fuzz harnesses generated by
                                 tools/gen-invariants.sh and class templates
                                 under reference/invariant_class_templates/.
  - tools/symbolic-ce-to-forge.py halmos CE → Forge replay scaffold (capv3
                                 iter8-T3). Used in forge_test_generated.
  - tools/poc-scaffold.py         pattern-based PoC scaffold generator. Used
                                 when no symbolic CE is available and the
                                 wrapper falls back to a generic Forge
                                 reproducer for a shrunk crash sequence.
  - tools/audit-deep.sh           profile-driven deep audit aggregator.
                                 Surfaced in summary.md as a follow-up
                                 pointer; the wrapper does NOT invoke
                                 audit-deep itself (out of scope, would
                                 spin up halmos/medusa/echidna chains).

Lifecycle (Codex's exact spec):

    created -> seeding -> running -> crash_found|timeout|coverage_plateau ->
    shrinking -> forge_test_generated -> verified|rejected

Artifact layout (Codex's exact spec):

    fuzz_campaigns/<id>/
      config.json                campaign config (lane='fuzz', target,
                                 profile, duration, engine)
      coverage.json              coverage manifest (engine-reported or
                                 honest-zero placeholder)
      crashes/*.json             one JSON per unique crash, with
                                 {crash_id, status, raw_seq, dedup_hash,
                                 source_run, captured_at}
      shrunk/*.json              one JSON per shrunk crash, with
                                 {crash_id, original_len, shrunk_len,
                                 shrunk_seq, method}
      forge_tests/*.t.sol        generated Forge replay tests
      replay.log                 chronological state-transition log
      summary.md                 human-readable campaign summary

First-implementation scope (PR 4):
  - Forge invariant/fuzz only via fuzz-runner.sh's existing engine layer.
  - Shrink uses a deterministic suffix-pruning strategy on the captured
    failing-sequence trace; non-deterministic Forge fuzz seeds are
    surfaced in the shrunk artifact's `seed_determinism` field.
  - Symbolic / Halmos / Medusa orchestration deferred to a follow-up PR.
    Stated explicitly in the PR body and in summary.md when the chosen
    engine is "auto" but only Forge is available.

Terminology constraint (Codex): this is NOT "infinite fuzzers". It is a
bounded campaign farm. Budget guards (--duration cap), crash dedupe (SHA
of normalized sequence), and resume behaviour (campaign-state init is
idempotent) are first-class.

CLI surface::

    python3 tools/fuzz-campaign.py \\
        --workspace ~/audits/<project> \\
        --target <Contract> \\
        --profile invariant \\
        --duration 3600 \\
        --out ~/audits/<project>/fuzz_campaigns/<id>

Honest-finding contract:
  - Every terminal state writes summary.md. timeout / coverage_plateau /
    rejected MUST NOT claim "verified". The "verified" state is only
    reachable when the generated Forge test reproduces on the
    vulnerable version AND changes behavior (or fails) on the
    recommended-fix version. That gate is operator-driven (pre/post-fix
    smoke); the wrapper records the evidence but does not auto-promote.

Stdlib-only: argparse, hashlib, json, pathlib, re, shutil, subprocess,
sys, time, datetime, typing.
"""
