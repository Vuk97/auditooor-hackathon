_FORGE_TEMPLATE = """// SPDX-License-Identifier: MIT
// AUTO-GENERATED by tools/fuzz-campaign.py — DO NOT EDIT BY HAND.
// crash_id: {crash_id}
// dedup_hash: {dedup_hash}
// captured_at: {captured_at}
// seed_determinism: {seed_determinism}
//
// This is a *replay scaffold* derived from a Forge invariant/fuzz crash.
// Operator must:
//   1. Wire setUp() against the actual contract under test.
//   2. Replace the placeholder calls below with the shrunk sequence.
//   3. Run `forge test --match-path forge_tests/{file_name}` on:
//        - the vulnerable commit  -> expect FAIL
//        - the recommended-fix    -> expect PASS (or different failure)
//      Only after BOTH are observed may the campaign transition to
//      `verified`. The wrapper records evidence; the operator promotes.
pragma solidity ^0.8.0;

import "forge-std/Test.sol";

contract Replay_{contract_id} is Test {{
    // TODO: import the contract under test
    // import "{target_path}";

    function setUp() public {{
        // TODO: deploy {target} with realistic constructor args.
    }}

    function test_replay_{crash_id_short}() public {{
        // ----- begin shrunk failing sequence (verbatim from campaign) -----
        /*
{sequence_block}
        */
        // ----- end shrunk failing sequence --------------------------------

        // TODO: translate the sequence above into concrete contract calls.
        // The wrapper does NOT auto-translate Forge sequence syntax into
        // Solidity calls — that requires interface knowledge the wrapper
        // does not have. See tools/poc-scaffold.py for pattern-aware
        // scaffolds, or tools/symbolic-ce-to-forge.py for halmos CE-driven
        // replays.
        assertTrue(false, "operator: fill in the replay then remove this assert");
    }}
}}
"""


def generate_forge_test(crash_id: str, dedup_hash: str,
                        target: str, shrunk_seq: str,
                        seed_determinism: str,
                        captured_at: str,
                        out_path: Path) -> Path:
    """Render a Forge replay-scaffold .t.sol file.

    The scaffold ALWAYS asserts false until an operator wires it. This is
    intentional: a green test from an unfilled scaffold would be the
    "shrink hides root cause" failure mode Minimax warned about.
    """
    contract_id = re.sub(r"[^A-Za-z0-9_]", "_", target) or "Target"
    file_name = out_path.name
    # crash_id is 'crash-<dedup>'; the function-name segment must be
    # ASCII identifier (no hyphens, dots, etc) for Solidity to compile.
    crash_id_short = re.sub(r"[^A-Za-z0-9_]", "_", crash_id[:18])
    seq_block = "\n".join(
        f"        {ln}" for ln in (shrunk_seq or "(empty)").splitlines()
    )
    body = _FORGE_TEMPLATE.format(
        crash_id=crash_id,
        crash_id_short=crash_id_short,
        dedup_hash=dedup_hash,
        captured_at=captured_at,
        seed_determinism=seed_determinism,
        contract_id=contract_id,
        target=target,
        target_path=f"src/{target}.sol",
        sequence_block=seq_block,
        file_name=file_name,
    )
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(body)
    return out_path


