def main(argv: list[str] | None = None) -> int:
    parser = build_parser()
    args = parser.parse_args(argv)

    if args.list_tools:
        _print_inventory()
        return 0

    try:
        state = run_campaign(
            workspace=args.workspace.resolve(),
            target=args.target,
            profile=args.profile,
            duration=args.duration,
            out_dir=args.out.resolve(),
            engine=args.engine,
        )
    except (ValueError, FileNotFoundError, RuntimeError) as e:
        print(f"[fuzz-campaign] error: {e}", file=sys.stderr)
        return 2

    print(f"[fuzz-campaign] lifecycle={state['lifecycle']} out={args.out}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
