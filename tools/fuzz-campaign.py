def _which(binary: str) -> str | None:
    return shutil.which(binary)


def _engine_available(engine: str) -> bool:
    if engine == "forge":
        return _which("forge") is not None
    if engine in ("medusa", "echidna"):
        return _which(engine) is not None
    return False


def discover_engine(workspace: Path, requested: str) -> str:
    """Pick an engine. Defaults to 'forge' (PR 4 first-impl scope).

    Returns the chosen engine name. Raises RuntimeError if the requested
    engine is not on PATH. Does NOT silently fall through — that would
    let a user think medusa is running when only forge is.
    """
    if requested == "auto":
        if _engine_available("forge"):
            return "forge"
        # PR 4 scope: do not orchestrate medusa/echidna ourselves. Surface a
        # clear error rather than pretending the wrapper supports them today.
        raise RuntimeError(
            "no fuzz engine on PATH. PR 4 wrapper supports forge only. "
            "medusa/echidna orchestration is deferred to a follow-up PR; "
            "for those engines invoke tools/fuzz-runner.sh directly."
        )
    if requested == "forge":
        if not _engine_available("forge"):
            raise RuntimeError("forge not on PATH")
        return "forge"
    if requested in ("medusa", "echidna"):
        # Defer cleanly — give the caller an actionable message. PR 4
        # explicitly defers medusa/echidna orchestration to a follow-up
        # PR; wrapper supports forge only today.
        raise RuntimeError(
            f"engine {requested!r} deferred to a follow-up PR. PR 4 "
            f"wrapper supports forge only. Use tools/fuzz-runner.sh "
            f"directly for medusa/echidna; symbolic/Halmos orchestration "
            f"is also deferred."
        )
    raise RuntimeError(f"unknown engine: {requested!r}")


def run_forge_campaign(workspace: Path, target: str, profile: str,
                       duration: int, out_dir: Path,
                       _runner_override: list[str] | None = None) -> dict[str, Any]:
    """Invoke `forge test --invariant` (or --fuzz) under a wall-clock cap.

    Returns a result dict with keys: status, exit_code, stdout_path,
    stderr_path, duration_s, command, engine_version. The status alphabet
    is the wrapper's vocabulary, mapped from forge exit codes:
      - "crash_found"   → forge exit != 0 AND crash markers present
      - "timeout"       → wall-clock exceeded
      - "coverage_plateau" → forge passed AND --duration >= 60 (heuristic;
                          v1 conservative — operator can override)
      - "no_finding"    → forge passed, short run, no crash and no plateau

    The `_runner_override` argv is for tests only — it lets the test inject
    a fake `forge` command without PATH-shadowing.
    """
    out_dir.mkdir(parents=True, exist_ok=True)
    stdout_path = out_dir / "forge.stdout.log"
    stderr_path = out_dir / "forge.stderr.log"

    # Build forge argv. The runner_override path lets tests inject a fake
    # forge that emits crash markers / pass / hang on demand.
    if _runner_override is not None:
        argv = list(_runner_override)
    else:
        # Use --match-contract to scope to the target. --fuzz-runs and
        # --invariant-runs are bounded so a misconfigured project cannot
        # blow past --duration on a single seed expansion.
        if profile == "invariant":
            argv = [
                "forge", "test",
                "--match-contract", target,
                "--invariant-runs", "256",
                "--invariant-depth", "32",
            ]
        else:  # fuzz
            argv = [
                "forge", "test",
                "--match-contract", target,
                "--fuzz-runs", "256",
            ]

    # Get engine version best-effort (only when not overridden — tests
    # don't care).
    engine_version = ""
    if _runner_override is None:
        try:
            v = subprocess.check_output(
                ["forge", "--version"], stderr=subprocess.DEVNULL, timeout=5
            )
            engine_version = v.decode("utf-8", "replace").strip().splitlines()[0]
        except (FileNotFoundError, subprocess.TimeoutExpired,
                subprocess.CalledProcessError):
            pass

    started = time.monotonic()
    try:
        proc = subprocess.run(
            argv,
            cwd=str(workspace),
            stdout=stdout_path.open("w"),
            stderr=stderr_path.open("w"),
            timeout=duration,
        )
        exit_code = proc.returncode
        timed_out = False
    except subprocess.TimeoutExpired:
        exit_code = 124
        timed_out = True
    elapsed = time.monotonic() - started

    stdout_text = stdout_path.read_text(errors="replace") if stdout_path.exists() else ""

    # Status classification
    crash_markers = bool(
        re.search(r"\b(FAIL|invariant.*failed|counterexample|panic|revert)\b",
                  stdout_text, re.IGNORECASE)
    )
    if timed_out:
        status = "timeout"
    elif crash_markers and exit_code != 0:
        status = "crash_found"
    elif exit_code == 0 and duration >= 60:
        # Conservative coverage-plateau heuristic: a clean pass under a
        # non-trivial budget. We do NOT call this "verified" — that would be
        # the dishonest classification Minimax warned about.
        status = "coverage_plateau"
    else:
        status = "no_finding"

    return {
        "status": status,
        "exit_code": exit_code,
        "stdout_path": str(stdout_path),
        "stderr_path": str(stderr_path),
        "duration_s": round(elapsed, 2),
        "command": " ".join(argv),
        "engine_version": engine_version,
    }


# ---------------------------------------------------------------------------
# Crash extraction + shrink
# ---------------------------------------------------------------------------

_CRASH_BLOCK_RE = re.compile(
    r"""
    (?:
        ^.*FAIL.*$                           # forge "[FAIL. Reason: ...]" lines
        |^.*invariant.*(?:failed|broken).*$  # invariant break
        |^\s*Counterexample:.*$              # inline counterexample
        |^\s*\[Sequence\].*$                 # forge invariant sequence header
    )
    """,
    re.IGNORECASE | re.MULTILINE | re.VERBOSE,
)


def extract_failing_sequence(stdout_text: str) -> str:
    """Pull the failing call sequence out of forge stdout.

    Forge invariant output prints the sequence between the [Sequence]
    header and the next blank line or the next "Test Result:" line. We
    capture the whole crash block + 40 trailing lines (sequence rarely
    longer than that for bounded depth=32).
    """
    if not stdout_text:
        return ""
    lines = stdout_text.splitlines()
    keep: list[str] = []
    in_block = False
    tail = 0
    for ln in lines:
        if not in_block and _CRASH_BLOCK_RE.search(ln):
            in_block = True
            keep.append(ln)
            continue
        if in_block:
            # Stop at blank-line gap after a sequence has started, OR a
            # "Test Result:" / "Suite result:" forge boundary.
            if re.match(r"^\s*(Test Result:|Suite result:|Ran \d+ test)", ln):
                break
            keep.append(ln)
            tail += 1
            if tail > 40:
                break
    return "\n".join(keep[:SHRUNK_MAX_LINES])


def shrink_sequence(failing_sequence: str) -> dict[str, Any]:
    """Deterministic shrink: prune trailing lines that are not part of
    the canonical [Sequence] block, drop blanks, dedup repeated ops.

    Returns a dict {original_len, shrunk_len, shrunk_seq, method,
    seed_determinism}. seed_determinism is "deterministic" when the input
    contains a "[Sequence]" header (Forge invariant) — otherwise
    "non-deterministic" (e.g. fuzz seeds, ad-hoc panic) and the operator
    is warned in summary.md.
    """
    raw = failing_sequence or ""
    original_len = len(raw.splitlines())
    seed_det = "deterministic" if "[Sequence]" in raw else "non-deterministic"

    # Step 1: keep only sequence-relevant lines.
    keep: list[str] = []
    seen_seq_header = False
    for ln in raw.splitlines():
        # Always keep crash header so the sequence is interpretable.
        if _CRASH_BLOCK_RE.search(ln):
            keep.append(ln.rstrip())
            if "[Sequence]" in ln:
                seen_seq_header = True
            continue
        if seen_seq_header and ln.strip():
            keep.append(ln.rstrip())

    # Step 2: dedup adjacent identical calls (common in invariant traces).
    deduped: list[str] = []
    for ln in keep:
        if not deduped or deduped[-1] != ln:
            deduped.append(ln)

    shrunk = "\n".join(deduped[:SHRUNK_MAX_LINES])
    return {
        "original_len": original_len,
        "shrunk_len": len(deduped),
        "shrunk_seq": shrunk,
        "method": "suffix-prune+dedup",
        "seed_determinism": seed_det,
    }


