# Failing-sequence normalization: strip volatile fields (timestamps,
# addresses, hex blob lengths) before hashing so two reruns of the same
# Forge fuzz with different seeds dedup to one crash.
_VOLATILE_PATTERNS = (
    (re.compile(r"0x[0-9a-fA-F]{40}"), "<addr>"),
    (re.compile(r"0x[0-9a-fA-F]{16,}"), "<hex>"),
    (re.compile(r"\bts=\d+"), "ts=<n>"),
    (re.compile(r"\btimestamp=\d+"), "timestamp=<n>"),
    (re.compile(r"\bgas:\s*\d+"), "gas: <n>"),
    (re.compile(r"\bseed:\s*\d+"), "seed: <n>"),
    (re.compile(r"\brun:\s*\d+"), "run: <n>"),
    # Forge invariant counters — different across reruns of the SAME crash.
    (re.compile(r"\bruns:\s*\d+"), "runs: <n>"),
    (re.compile(r"\bcalls:\s*\d+"), "calls: <n>"),
    (re.compile(r"\breverts:\s*\d+"), "reverts: <n>"),
    # Generic large numbers AFTER the specific patterns above so we don't
    # eat single-digit args like deposit(100). Threshold = 10 digits to
    # avoid touching small literals in the call sequence.
    (re.compile(r"\b\d{10,}\b"), "<num>"),
)


def normalize_sequence(text: str) -> str:
    """Normalize a failing-sequence trace for dedup hashing.

    Public for unit-test use. Strips volatile addresses/hex/numbers that
    differ across reruns of the same crash but preserves the call shape.
    """
    out = text
    for rx, repl in _VOLATILE_PATTERNS:
        out = rx.sub(repl, out)
    # Collapse repeated whitespace; preserve newlines for sequence shape.
    out = re.sub(r"[ \t]+", " ", out)
    return out.strip()


def crash_dedup_hash(failing_sequence: str) -> str:
    """First 12 chars of SHA256 over normalized sequence."""
    norm = normalize_sequence(failing_sequence)
    return hashlib.sha256(norm.encode("utf-8")).hexdigest()[:12]


