def build_parser() -> argparse.ArgumentParser:
    p = argparse.ArgumentParser(
        prog="fuzz-campaign",
        description=(
            "V5 fuzz-campaign wrapper. Orchestrates existing fuzz tooling "
            "(tools/fuzz-runner.sh, forge invariant, tools/symbolic-ce-to-forge.py, "
            "tools/poc-scaffold.py). Bounded campaign farm — NOT 'infinite "
            "fuzzers'."
        ),
    )
    p.add_argument("--workspace", required=True, type=Path,
                   help="Path to the audit workspace (foundry root).")
    p.add_argument("--target", required=True,
                   help="Contract name to fuzz (forge --match-contract).")
    p.add_argument("--profile", default="invariant", choices=PROFILES,
                   help="Fuzz profile (default: invariant).")
    p.add_argument("--duration", type=int, default=600,
                   help=f"Wall-clock cap in seconds (max {DURATION_CAP_SECONDS}).")
    p.add_argument("--out", required=True, type=Path,
                   help="Campaign output dir, e.g. <ws>/fuzz_campaigns/<id>.")
    p.add_argument("--engine", default="auto", choices=("auto", "forge", "medusa", "echidna"),
                   help="Fuzz engine. PR 4 supports 'forge' only; "
                        "medusa/echidna deferred to follow-up.")
    p.add_argument("--list-tools", action="store_true",
                   help="Print the inventory of orchestrated tools and exit.")
    return p


