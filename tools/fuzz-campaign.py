def can_transition(from_state: str, to_state: str) -> bool:
    """Return True iff the state machine permits from→to.

    Public for unit tests. Foot-gun guard: a wrapper bug that tries to
    write 'verified' directly from 'timeout' is a silent honesty bypass —
    we reject it loudly.
    """
    if from_state not in _TRANSITIONS:
        return False
    return to_state in _TRANSITIONS[from_state]


def transition(state: dict[str, Any], to_state: str, replay_log: Path,
               **details: Any) -> None:
    """Mutate state['lifecycle'] with validation + replay log entry."""
    cur = state.get("lifecycle", "created")
    if not can_transition(cur, to_state):
        raise ValueError(
            f"illegal transition {cur!r} -> {to_state!r} "
            f"(allowed: {sorted(_TRANSITIONS.get(cur, ()))})"
        )
    state["lifecycle"] = to_state
    state["updated_at"] = _utcnow_iso()
    _append_replay(replay_log, f"transition:{to_state}",
                   from_state=cur, **details)


