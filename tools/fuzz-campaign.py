def _seed_artifacts(out_dir: Path, target: str, profile: str,
                    duration: int, engine: str, campaign_id: str) -> None:
    """Create the empty artifact tree so partial runs are inspectable."""
    out_dir.mkdir(parents=True, exist_ok=True)
    (out_dir / "crashes").mkdir(exist_ok=True)
    (out_dir / "shrunk").mkdir(exist_ok=True)
    (out_dir / "forge_tests").mkdir(exist_ok=True)

    config = {
        "campaign_id": campaign_id,
        "target": target,
        "profile": profile,
        "budget_seconds": duration,
        "engine": engine,
        "created_at": _utcnow_iso(),
        "schema_version": "fuzz_campaign.v1",
    }
    _atomic_write_json(out_dir / "config.json", config)
    # Honest-zero placeholder for coverage. Real coverage extraction is a
    # follow-up PR.
    _atomic_write_json(out_dir / "coverage.json", {
        "schema_version": "fuzz_coverage.v1",
        "engine": engine,
        "coverage": None,
        "note": "coverage extraction deferred to follow-up PR; honest-zero placeholder",
    })


def run_campaign(workspace: Path, target: str, profile: str,
                 duration: int, out_dir: Path, engine: str = "auto",
                 _runner_override: list[str] | None = None,
                 _stdout_override: str | None = None) -> dict[str, Any]:
    """End-to-end campaign driver.

    Test hooks:
      _runner_override   replaces the forge argv (subprocess still runs).
      _stdout_override   bypasses the engine entirely; the given text is
                         treated as forge stdout. status is inferred via
                         the same crash-marker logic. Used for hermetic
                         tests when forge is not on PATH.

    Returns the final state dict (also persisted to out_dir/summary.json).
    """
    if profile not in PROFILES:
        raise ValueError(f"invalid profile {profile!r}; expected one of {PROFILES}")
    if duration <= 0 or duration > DURATION_CAP_SECONDS:
        raise ValueError(
            f"duration must be in 1..{DURATION_CAP_SECONDS} (got {duration})"
        )
    if not workspace.is_dir():
        raise FileNotFoundError(f"workspace not found: {workspace}")

    campaign_id = out_dir.name
    replay_log = out_dir / "replay.log"

    # Engine selection. _stdout_override skips real engine.
    if _stdout_override is None:
        chosen_engine = discover_engine(workspace, engine)
    else:
        chosen_engine = "forge"  # fixture-only

    _seed_artifacts(out_dir, target, profile, duration, chosen_engine,
                    campaign_id)

    state: dict[str, Any] = {
        "campaign_id": campaign_id,
        "lifecycle": "created",
        "target": target,
        "profile": profile,
        "duration": 0,
        "budget_seconds": duration,
        "engine": chosen_engine,
        "started_at": _utcnow_iso(),
        "crashes": [],
        "shrunk": [],
        "forge_tests": [],
        "notes": [],
    }
    _append_replay(replay_log, "campaign-init", **{
        "campaign_id": campaign_id, "target": target, "profile": profile,
        "budget_seconds": duration, "engine": chosen_engine,
    })

    # created -> seeding
    transition(state, "seeding", replay_log)

    # seeding -> running
    transition(state, "running", replay_log)

    # Run engine
    if _stdout_override is not None:
        # Hermetic test path: classify the override stdout directly.
        crash_markers = bool(
            re.search(r"\b(FAIL|invariant.*failed|counterexample|panic|revert)\b",
                      _stdout_override, re.IGNORECASE)
        )
        # Persist the override as if forge wrote it — keeps artifact shape
        # identical to a real run.
        (out_dir / "forge.stdout.log").write_text(_stdout_override)
        (out_dir / "forge.stderr.log").write_text("")
        if "__SIM_TIMEOUT__" in _stdout_override:
            run_result = {
                "status": "timeout",
                "exit_code": 124,
                "stdout_path": str(out_dir / "forge.stdout.log"),
                "stderr_path": str(out_dir / "forge.stderr.log"),
                "duration_s": float(duration),
                "command": "(simulated)",
                "engine_version": "simulated",
            }
        elif crash_markers:
            run_result = {
                "status": "crash_found",
                "exit_code": 1,
                "stdout_path": str(out_dir / "forge.stdout.log"),
                "stderr_path": str(out_dir / "forge.stderr.log"),
                "duration_s": min(float(duration), 1.0),
                "command": "(simulated)",
                "engine_version": "simulated",
            }
        else:
            run_result = {
                "status": ("coverage_plateau" if duration >= 60 else "no_finding"),
                "exit_code": 0,
                "stdout_path": str(out_dir / "forge.stdout.log"),
                "stderr_path": str(out_dir / "forge.stderr.log"),
                "duration_s": min(float(duration), 1.0),
                "command": "(simulated)",
                "engine_version": "simulated",
            }
    else:
        run_result = run_forge_campaign(
            workspace, target, profile, duration, out_dir,
            _runner_override=_runner_override,
        )

    rstatus = run_result["status"]

    # Branch in lifecycle
    if rstatus == "timeout":
        transition(state, "timeout", replay_log,
                   reason="wall-clock cap reached",
                   duration_s=run_result["duration_s"])
        _finalize_runtime(state)
        state["notes"].append(
            "timeout: bounded budget exhausted with no crash. Honest "
            "non-finding — re-run with longer --duration or different "
            "--target to escalate."
        )
        _atomic_write_json(out_dir / "summary.json", state)
        render_summary(state, out_dir)
        return state

    if rstatus == "coverage_plateau":
        transition(state, "coverage_plateau", replay_log,
                   reason="forge passed clean under non-trivial budget",
                   duration_s=run_result["duration_s"])
        _finalize_runtime(state)
        state["notes"].append(
            "coverage_plateau: forge passed without producing a crash "
            "across {}s configured budget. NOT a clean-bill-of-health.".format(duration)
        )
        _atomic_write_json(out_dir / "summary.json", state)
        render_summary(state, out_dir)
        return state

    if rstatus == "no_finding":
        # Short bounded run with no crash and no plateau. We treat this as
        # rejected to honor the lifecycle's terminal-state contract.
        # (Lifecycle has no "no_finding" state.) Get there via
        # coverage_plateau honest classification + a note.
        transition(state, "coverage_plateau", replay_log,
                   reason="short clean run; reclassified as plateau",
                   duration_s=run_result["duration_s"])
        _finalize_runtime(state)
        state["notes"].append(
            "short run with no crash; reclassified to coverage_plateau "
            "to keep lifecycle honest. Increase --duration for confidence."
        )
        _atomic_write_json(out_dir / "summary.json", state)
        render_summary(state, out_dir)
        return state

    if rstatus != "crash_found":
        raise RuntimeError(f"unexpected run status: {rstatus!r}")

    # crash_found path
    transition(state, "crash_found", replay_log,
               exit_code=run_result["exit_code"],
               duration_s=run_result["duration_s"])

    stdout_text = (
        _stdout_override if _stdout_override is not None
        else (out_dir / "forge.stdout.log").read_text(errors="replace")
    )
    failing_seq = extract_failing_sequence(stdout_text)
    dedup = crash_dedup_hash(failing_seq) if failing_seq else "no-seq"
    crash_id = f"crash-{dedup}"
    crash_artifact = {
        "crash_id": crash_id,
        "dedup_hash": dedup,
        "raw_seq": failing_seq[:32_000],  # bound — log mass-flooded by big traces
        "exit_code": run_result["exit_code"],
        "duration_s": run_result["duration_s"],
        "captured_at": _utcnow_iso(),
        "engine": chosen_engine,
        "engine_version": run_result.get("engine_version", ""),
    }
    crash_path = out_dir / "crashes" / f"{crash_id}.json"
    _atomic_write_json(crash_path, crash_artifact)
    state["crashes"].append(crash_id)

    # crash_found -> shrinking
    transition(state, "shrinking", replay_log, crash_id=crash_id)
    shrunk = shrink_sequence(failing_seq)
    shrunk_artifact = {
        "crash_id": crash_id,
        "dedup_hash": dedup,
        "shrunk_at": _utcnow_iso(),
        **shrunk,
    }
    shrunk_path = out_dir / "shrunk" / f"{crash_id}.json"
    _atomic_write_json(shrunk_path, shrunk_artifact)
    state["shrunk"].append(crash_id)

    # shrinking -> forge_test_generated
    transition(state, "forge_test_generated", replay_log, crash_id=crash_id)
    forge_test_path = out_dir / "forge_tests" / f"Replay_{dedup}.t.sol"
    generate_forge_test(
        crash_id=crash_id,
        dedup_hash=dedup,
        target=target,
        shrunk_seq=shrunk["shrunk_seq"],
        seed_determinism=shrunk["seed_determinism"],
        captured_at=crash_artifact["captured_at"],
        out_path=forge_test_path,
    )
    state["forge_tests"].append(forge_test_path.name)

    # forge_test_generated is a PAUSE. The wrapper deliberately does NOT
    # auto-transition to verified/rejected — that requires running the
    # generated test against vulnerable + fixed commits, which the
    # operator owns. The summary.md records this gate explicitly.
    _finalize_runtime(state)
    state["notes"].append(
        "forge_test_generated reached. Operator must run the generated "
        "Replay_{}.t.sol on vulnerable + fixed commits to advance to "
        "verified|rejected.".format(dedup)
    )
    _atomic_write_json(out_dir / "summary.json", state)
    render_summary(state, out_dir)
    return state


