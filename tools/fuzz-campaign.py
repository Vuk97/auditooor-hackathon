def _utcnow_iso() -> str:
    return _dt.datetime.now(_dt.timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def _elapsed_seconds(started_at: str, ended_at: str) -> int:
    try:
        start = _dt.datetime.fromisoformat(started_at.replace("Z", "+00:00"))
        end = _dt.datetime.fromisoformat(ended_at.replace("Z", "+00:00"))
    except (TypeError, ValueError):
        return 0
    return max(0, int((end - start).total_seconds()))


def _finalize_runtime(state: dict[str, Any]) -> None:
    state["ended_at"] = _utcnow_iso()
    state["duration"] = _elapsed_seconds(
        str(state.get("started_at", "")),
        str(state.get("ended_at", "")),
    )


def _atomic_write_json(path: Path, payload: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp = path.with_suffix(path.suffix + ".tmp")
    tmp.write_text(json.dumps(payload, indent=2, sort_keys=True))
    tmp.replace(path)


def _append_replay(path: Path, event: str, **fields: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    line = {"ts": _utcnow_iso(), "event": event, **fields}
    with path.open("a") as f:
        f.write(json.dumps(line, sort_keys=True) + "\n")


