def render_summary(state: dict[str, Any], out_dir: Path) -> Path:
    """Write summary.md. Honest non-finding for timeout/coverage_plateau.

    Returns the path written.
    """
    lc = state.get("lifecycle", "unknown")
    target = state.get("target", "?")
    profile = state.get("profile", "?")
    duration = state.get("duration", 0)
    budget_seconds = state.get("budget_seconds", duration)
    engine = state.get("engine", "?")
    crashes = state.get("crashes", [])
    shrunk = state.get("shrunk", [])
    forge_tests = state.get("forge_tests", [])
    started = state.get("started_at", "?")
    ended = state.get("ended_at", _utcnow_iso())
    notes = state.get("notes", [])

    if lc in ("timeout", "coverage_plateau"):
        verdict = (
            f"**Honest non-finding.** Status: `{lc}`. The campaign did not "
            f"produce a verified crash within the bounded duration "
            f"({duration}s elapsed, {budget_seconds}s budget). This is NOT "
            f"a clean bill of health — it means "
            f"this run's tooling did not surface a finding under these "
            f"settings. Re-run with longer --duration, broader --target, "
            f"or escalate to symbolic/medusa via tools/fuzz-runner.sh."
        )
    elif lc == "verified":
        verdict = (
            "**Verified replay scaffold generated.** A Forge replay test "
            "was produced from a shrunk crash. Operator must run it on the "
            "vulnerable + fixed commits; only the operator can promote a "
            "candidate to a finding."
        )
    elif lc == "rejected":
        verdict = (
            "**Rejected.** Crash was reproduced but did NOT survive "
            "operator gate (e.g. could not be made to fail on vulnerable "
            "version OR did not change behavior on fix)."
        )
    elif lc == "forge_test_generated":
        verdict = (
            "**Forge test generated; awaiting operator promotion gate.** "
            "Run the test on vulnerable + fixed commits to advance to "
            "`verified` or `rejected`."
        )
    else:
        verdict = f"In-flight or interrupted (lifecycle={lc})."

    lines = [
        f"# Fuzz campaign summary — {state.get('campaign_id', '?')}",
        "",
        f"- target: `{target}`",
        f"- profile: `{profile}`",
        f"- engine: `{engine}`",
        f"- elapsed duration: `{duration}s`",
        f"- duration cap: `{budget_seconds}s`",
        f"- started_at: `{started}`",
        f"- ended_at: `{ended}`",
        f"- lifecycle: `{lc}`",
        "",
        "## Verdict",
        "",
        verdict,
        "",
        "## Inventory of orchestrated tools",
        "",
        "- `tools/fuzz-runner.sh` (PR 107 bounded engine layer)",
        "- `forge test --invariant` / `--fuzz`",
        "- `tools/symbolic-ce-to-forge.py` (advisory, not run in this PR's "
        "first impl)",
        "- `tools/poc-scaffold.py` (advisory pattern templates)",
        "",
        "## Counts",
        "",
        f"- crashes: {len(crashes)}",
        f"- shrunk: {len(shrunk)}",
        f"- forge_tests: {len(forge_tests)}",
        "",
        "## Deferred to follow-up PR",
        "",
        "- Symbolic / Halmos / Medusa orchestration",
        "- Coverage-plateau detection from real coverage.json (today: heuristic)",
        "- Auto-promotion based on pre/post-fix replay (today: operator-driven)",
        "",
        "## Notes",
        "",
        *(f"- {n}" for n in notes),
        "",
    ]
    p = out_dir / "summary.md"
    p.write_text("\n".join(lines))
    return p


