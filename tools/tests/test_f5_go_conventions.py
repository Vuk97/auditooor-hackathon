"""F5: Go scaffolding conventions are OOS; production Go is never false-dropped.

Before F5 the bare-word 'mock'/'_mock' markers matched a DIR segment but not a
basename suffix, so `span_mock.go` / `genesis_contract_mock.go` leaked into the
in-scope set; `simulated.go` (the geth/bor SimulatedBackend test harness) had NO
marker at all -> the polygon asymmetry generator paired production code against it.
The #1 sin is the inverse (dropping real protocol source), so the production-kept
assertions are as important as the OOS ones.
"""
import sys
import unittest
from pathlib import Path

_LIB = Path(__file__).resolve().parent.parent / "lib"
if str(_LIB) not in sys.path:
    sys.path.insert(0, str(_LIB))
import scope_exclusion as se  # noqa: E402


class TestGoConventions(unittest.TestCase):
    def test_mock_fake_stub_basename_suffix_oos(self):
        for p in (
            "x/foo/keeper/foo_mock.go",
            "consensus/bor/span_mock.go",
            "x/bar/genesis_contract_mock.go",
            "a/b/thing_fake.go",
            "a/b/thing_stub.go",
            "pkg/svc/client_mocks.go",
        ):
            self.assertTrue(se.is_test(p), f"is_test should be True for {p}")
            self.assertTrue(se.is_oos(p), f"is_oos should be True for {p}")

    def test_simulated_backend_oos(self):
        for p in (
            "accounts/abi/bind/backends/simulated.go",
            "x/foo/backend_simulated.go",
        ):
            self.assertTrue(se.is_test(p), p)
            self.assertTrue(se.is_oos(p), p)

    def test_production_go_kept(self):
        # must NOT false-drop real production go (the #1 sin). Note: /app/ and
        # /migrations/ are deliberately NOT test markers (cosmos app.go and module
        # migration handlers are production), and 'simulated' must not fire on a
        # substring inside an unrelated word.
        for p in (
            "consensus/bor/bor.go",
            "x/staking/keeper/keeper.go",
            "core/genesis.go",
            "x/foo/migrations/v2.go",
            "app/app.go",
            "latest_state.go",
            "core/unsimulated_path.go",  # 'simulated' substring must NOT drop it
        ):
            self.assertFalse(se.is_oos(p), f"is_oos should be False (in scope) for {p}")

    def test_abigen_binding_header_is_load_bearing(self):
        head = ("// Code generated - DO NOT EDIT.\n"
                "// Code generated by abigen.\npackage bindings\n")
        # with the head, the DO-NOT-EDIT catch fires even with no .pb/.abigen name
        self.assertTrue(se.is_generated("contracts/bindings/erc20.go", head=head))
        self.assertTrue(se.is_oos("contracts/bindings/erc20.go", head=head))
        # without the head, an un-conventionally-named binding is NOT detectable as
        # generated -> proves head= is the load-bearing signal the heatmap now passes.
        self.assertFalse(se.is_generated("contracts/bindings/erc20.go"))


if __name__ == "__main__":
    unittest.main(verbosity=2)
