#!/usr/bin/env python3
"""Guard tests for the coverage-denominator generated/test-infra exclusions in
workspace-coverage-heatmap.py.

A coverage denominator padded with machine-generated code (protobuf `.pb.go`,
abigen ETH bindings) and test infrastructure (`interchaintest/`, `testutil/`,
`*_test_suite.go`, test contracts) is a structural false-red: those units are
not an auditable surface, so no hunt can ever "scan" them and the gate can never
go green. These tests pin the exclusions AND that real production source still
survives (no over-prune)."""
from __future__ import annotations

import importlib.util
import tempfile
import unittest
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]
_spec = importlib.util.spec_from_file_location("hm", ROOT / "tools" / "workspace-coverage-heatmap.py")
hm = importlib.util.module_from_spec(_spec)
_spec.loader.exec_module(hm)  # type: ignore[union-attr]

_GEN_HEADER = "// Code generated by protoc-gen-gogo. DO NOT EDIT.\n"
_GO_FN = "package x\nfunc {name}() int {{ return 1 }}\n"
_SOL_FN = "// SPDX\npragma solidity ^0.8;\ncontract C {{ function {name}() public {{}} }}\n"


def _ws(files: dict[str, str]) -> Path:
    ws = Path(tempfile.mkdtemp(prefix="cov_excl_"))
    src = ws / "src"
    for rel, content in files.items():
        p = src / rel
        p.parent.mkdir(parents=True, exist_ok=True)
        p.write_text(content, encoding="utf-8")
    return ws


def _unit_files(ws: Path) -> set[str]:
    units, _ = hm.enumerate_units(ws)
    # reduce each unit key to its source basename for assertions
    return {u.split("::")[0].split("--")[0] for u in units}


class TestGeneratedExclusion(unittest.TestCase):
    def test_generated_header_go_excluded(self):
        ws = _ws({
            "real_keeper.go": _GO_FN.format(name="Withdraw"),
            "account_pb.go": _GEN_HEADER + _GO_FN.format(name="XXX_Marshal"),  # header, non-.pb name
        })
        files = _unit_files(ws)
        self.assertIn("real_keeper.go", files)
        self.assertNotIn("account_pb.go", files)

    def test_pb_and_abigen_filename_excluded(self):
        ws = _ws({
            "types/auction.pb.go": _GO_FN.format(name="Size"),          # no header, .pb.go name
            "types/query.pb.gw.go": _GO_FN.format(name="RegisterQ"),
            "hooks/permissions.abigen.go": _GO_FN.format(name="NewBind"),
            "keeper/real.go": _GO_FN.format(name="Settle"),
        })
        files = _unit_files(ws)
        self.assertIn("keeper/real.go".split("/")[-1], {f.split("/")[-1] for f in files})
        for gen in ("auction.pb.go", "query.pb.gw.go", "permissions.abigen.go"):
            self.assertNotIn(gen, {f.split("/")[-1] for f in files}, gen)


class TestTestInfraExclusion(unittest.TestCase):
    def test_test_infra_dirs_pruned(self):
        ws = _ws({
            "modules/evm/testutil/base_test_suite.go": _GO_FN.format(name="BuildEthTx"),
            "modules/peggy/testpeggy/suite.go": _GO_FN.format(name="Setup"),
            "interchaintest/helpers/peggy.go": _GO_FN.format(name="Bridge"),
            "modules/peggy/keeper/keeper.go": _GO_FN.format(name="Attest"),  # real
        })
        files = {f.split("/")[-1] for f in _unit_files(ws)}
        self.assertIn("keeper.go", files)
        for t in ("base_test_suite.go", "suite.go", "peggy.go"):
            self.assertNotIn(t, files, t)

    def test_test_helper_go_filenames(self):
        ws = _ws({
            "app/test_helpers.go": _GO_FN.format(name="NewTestApp"),
            "app/test_suite.go": _GO_FN.format(name="Suite"),
            "app/app.go": _GO_FN.format(name="New"),  # real
        })
        files = {f.split("/")[-1] for f in _unit_files(ws)}
        self.assertIn("app.go", files)
        self.assertNotIn("test_helpers.go", files)
        self.assertNotIn("test_suite.go", files)


class TestRealSourceSurvives(unittest.TestCase):
    def test_normal_source_included(self):
        ws = _ws({
            "modules/exchange/keeper/derivative.go": _GO_FN.format(name="MatchOrders"),
            "contracts/Peggy.sol": _SOL_FN.format(name="submitBatch"),
            "modules/oracle/keeper/attestation.go": _GO_FN.format(name="Tally"),  # 'test' substring, NOT a test file
        })
        files = {f.split("/")[-1] for f in _unit_files(ws)}
        self.assertIn("derivative.go", files)
        self.assertIn("attestation.go", files)  # must NOT be excluded by 'test' substring
        sol_units, _ = hm.enumerate_units(ws)
        self.assertTrue(any("Peggy.sol::submitBatch" in u for u in sol_units))


class TestHelperUnit(unittest.TestCase):
    def test_is_generated_source_file(self):
        ws = Path(tempfile.mkdtemp(prefix="cov_gen_"))
        gen = ws / "g.go"; gen.write_text(_GEN_HEADER + "package x\n")
        real = ws / "r.go"; real.write_text("package x\nfunc F() {}\n")
        self.assertTrue(hm._is_generated_source_file(str(gen)))
        self.assertFalse(hm._is_generated_source_file(str(real)))

    def test_test_infra_dir_regex(self):
        for d in ("testutil", "testdata", "testpeggy", "testtxfees", "testing"):
            self.assertTrue(hm._TEST_INFRA_DIR_RE.match(d), d)
        for d in ("keeper", "types", "exchange", "attestation"):
            self.assertFalse(hm._TEST_INFRA_DIR_RE.match(d), d)


class TestSolidityTestContractExclusion(unittest.TestCase):
    def test_foundry_test_and_script_contracts_excluded(self):
        ws = _ws({
            "Vault.sol": _SOL_FN.format(name="withdraw"),       # real - kept
            "Vault.t.sol": _SOL_FN.format(name="testWithdraw"),  # Foundry test - excluded
            "Deploy.s.sol": _SOL_FN.format(name="run"),          # Foundry script - excluded
        })
        units, _ = hm.enumerate_units(ws)
        bases = {u.split("::")[0].split("/")[-1] for u in units}
        self.assertIn("Vault.sol", bases)
        self.assertNotIn("Vault.t.sol", bases)
        self.assertNotIn("Deploy.s.sol", bases)
        self.assertTrue(any("Vault.sol::withdraw" in u for u in units))


if __name__ == "__main__":
    unittest.main(verbosity=2)
