class TestOperatorApprovalGate(unittest.TestCase):
    """FIX 1: a disposition shrinks a required gate's denominator (a per-gate
    rebuttal) so it must carry an operator-signed approval token. Fail-closed."""

    def _ws_one_config_disposition(self, approve):
        ws = _WS()
        ws.file("src/cfg/src/Cfg.sol", "contract Cfg {}")
        ws.dispositions([{"repo": "cfg", "classification": "non-economic-rationale",
                          "rationale": _good_rationale()}], approve=approve)
        return ws

    def test_agent_authored_no_approval_NOT_credited(self):
        # An agent-authored disposition with NO operator approval must be dropped:
        # the gate denominator is unchanged, the unit stays in the floor.
        ws = self._ws_one_config_disposition(approve=False)
        self.assertEqual(ned.load_dispositions(ws.dir), [],
                         "an unapproved disposition must NEVER credit")

    def test_default_agent_write_token_NOT_credited(self):
        # The exact token an agent already holds (scope 'write') must not green it:
        # only the deliberate 'disposition-approve' scope credits.
        ws = self._ws_one_config_disposition(approve=_agent_write_token(
            _WS().dir))  # wrong-ws + wrong-scope token
        self.assertEqual(ned.load_dispositions(ws.dir), [])

    def test_operator_approved_IS_credited(self):
        ws = self._ws_one_config_disposition(approve=True)
        accepted = ned.load_dispositions(ws.dir)
        self.assertEqual({d["repo"] for d in accepted}, {"cfg"},
                         "an operator-approved disposition must be credited")

    def test_per_entry_approval_ref_credits(self):
        # approval_ref may live on the row itself (not just the artifact top level).
        ws = _WS()
        ws.file("src/cfg/src/Cfg.sol", "contract Cfg {}")
        tok = _operator_approval_token(ws.dir)
        ws.file(".auditooor/non_economic_dispositions.json", json.dumps(
            {"schema": ned.SCHEMA, "dispositions": [
                {"repo": "cfg", "classification": "oos",
                 "rationale": _good_rationale(), "approval_ref": tok}]}))
        self.assertEqual({d["repo"] for d in ned.load_dispositions(ws.dir)}, {"cfg"})

    def test_wrong_scope_token_NOT_credited(self):
        # A validly-signed operator token WITHOUT the disposition-approve scope
        # (fail-closed on scope).
        ws = _WS()
        ws.file("src/cfg/src/Cfg.sol", "contract Cfg {}")
        bad, _ = _tok.issue_token(str(ws.dir), owner="operator",
                                  scope=["read", "write"], log=False)
        ws.dispositions([{"repo": "cfg", "classification": "oos",
                          "rationale": _good_rationale()}], approve=bad)
        self.assertEqual(ned.load_dispositions(ws.dir), [])

    def test_wrong_workspace_token_NOT_credited(self):
        # A token minted for a DIFFERENT workspace must not credit here.
        ws = _WS()
        ws.file("src/cfg/src/Cfg.sol", "contract Cfg {}")
        other = _operator_approval_token(_WS().dir)
        ws.dispositions([{"repo": "cfg", "classification": "oos",
                          "rationale": _good_rationale()}], approve=other)
        self.assertEqual(ned.load_dispositions(ws.dir), [])


class TestValueMovingFloorBreakdown(unittest.TestCase):
    """FIX 2: the honesty-check must expose proven-by-executed-code vs
    dispositioned(operator-approved) vs uncovered as THREE distinct numbers -
    never fold scope-removal into proof/coverage."""

    def setUp(self):
        self.mod = _load("ah_break", _TOOLS / "audit-honesty-check.py")

    def test_three_distinct_counts(self):
        ws = _WS()
        ws.file("src/cfg/src/Cfg.sol", "contract Cfg {}")
        ws.file("src/vault/src/Vault.sol", "contract Vault {}")
        vmf = {"function_count": 2, "functions": [
            {"file": "src/cfg/src/Cfg.sol", "function": "setCfg", "language": "sol",
             "transfer_hit": False, "ledger_write_hit": True},
            {"file": "src/vault/src/Vault.sol", "function": "withdraw",
             "language": "sol", "transfer_hit": True, "ledger_write_hit": True},
        ]}
        ws.value_moving(vmf["functions"])
        # cfg is dispositioned (operator-approved); vault is a real transfer-mover
        # with NO proof -> uncovered. Nothing is proven by executed code.
        ws.dispositions([{"repo": "cfg", "classification": "non-economic-rationale",
                          "rationale": _good_rationale()}], approve=True)
        detail = self.mod._value_moving_floor_breakdown(ws.dir, vmf)
        self.assertEqual(detail["value_moving_total"], 2)
        self.assertEqual(detail["dispositioned"], 1, "cfg is operator-dispositioned")
        self.assertEqual(detail["uncovered"], 1, "vault has no proof -> uncovered")
        self.assertEqual(detail["proven_by_executed_code"], 0)
        # dispositioned is NEVER folded into proven/uncovered:
        self.assertEqual(
            detail["proven_by_executed_code"] + detail["uncovered"],
            detail["value_moving_total"] - detail["dispositioned"])

    def test_unapproved_disposition_stays_uncovered_not_dispositioned(self):
        # Without operator approval, the config unit is NOT dispositioned - it
        # remains in the floor as uncovered (denominator unchanged).
        ws = _WS()
        ws.file("src/cfg/src/Cfg.sol", "contract Cfg {}")
        vmf = {"function_count": 1, "functions": [
            {"file": "src/cfg/src/Cfg.sol", "function": "setCfg", "language": "sol",
             "transfer_hit": False, "ledger_write_hit": True}]}
        ws.value_moving(vmf["functions"])
        ws.dispositions([{"repo": "cfg", "classification": "non-economic-rationale",
                          "rationale": _good_rationale()}], approve=False)
        detail = self.mod._value_moving_floor_breakdown(ws.dir, vmf)
        self.assertEqual(detail["dispositioned"], 0)
        self.assertEqual(detail["uncovered"], 1)


