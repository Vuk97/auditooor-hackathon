    def test_floor_drops_only_dispositioned_config_write(self):
        ws = _WS()
        ws.file("src/cfg/src/Cfg.sol", "contract Cfg {}")
        ws.file("src/vault/src/Vault.sol", "contract Vault {}")
        vmf = {"function_count": 2, "functions": [
            {"file": "src/cfg/src/Cfg.sol", "function": "setCfg", "language": "sol",
             "transfer_hit": False, "ledger_write_hit": True},
            {"file": "src/vault/src/Vault.sol", "function": "withdraw",
             "language": "sol", "transfer_hit": True, "ledger_write_hit": True},
        ]}
        ws.value_moving(vmf["functions"])
