        proc = _run_pipeline(self.workspace, "test-cluster", self.out_dir, self.tools_override)
        self.assertEqual(proc.returncode, 1)
        state = _read_state(self.out_dir)
        self.assertEqual(state["overall_status"], "BLOCKED")
        self.assertEqual(state["step_results"]["4"]["status"], "BLOCKED")
