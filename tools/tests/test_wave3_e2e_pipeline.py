#!/usr/bin/env python3
"""Regression tests for tools/wave3-e2e-pipeline.py (PR #729, Wave-3 item #5).

Covers the operator brief's six required cases:

  1. Single-cluster full-pipeline COMPLETE (synthetic mocks of sibling tools)
  2. Pipeline WAITING_FOR_OPERATOR after step 2
  3. Pipeline BLOCKED on originality scanner (step 4 exit 3)
  4. Pipeline DEGRADED on packager WARNING (step 5 exit 2)
  5. --steps 1,2 partial execution
  6. State file pipeline_state.json parses on resume

Plus extra coverage:

  7. SKIPPED behavior when sibling tools are absent (Wave-3 items #1-#4
     still in parallel build)
  8. --strict flips degraded into exit 1

Stdlib-only. Hermetic - we synthesize the 4 sibling tools as tiny Python
scripts in a tempdir and point the pipeline at them via AUDITOOOR_WAVE3_TOOLS_DIR.
"""
from __future__ import annotations

import json
import os
import re
import shutil
import subprocess
import sys
import tempfile
import unittest
from pathlib import Path
from typing import Dict, List


ROOT = Path(__file__).resolve().parents[2]
PIPELINE = ROOT / "tools" / "wave3-e2e-pipeline.py"


# A mock sibling tool. We render this with a desired exit code substituted in
# and write it into the tools-override dir.
MOCK_TOOL_TEMPLATE = """#!/usr/bin/env python3
# synthetic_fixture: true
# auto-generated by test_wave3_e2e_pipeline.py
import sys
from pathlib import Path

# Emit an output file if one of the standard output flags is present.
OUTPUT_FLAGS = ("--out", "--output")
OUTPUT_DIR_FLAGS = ("--out-dir",)

args = sys.argv[1:]
i = 0
while i < len(args):
    a = args[i]
    if a in OUTPUT_FLAGS and i + 1 < len(args):
        p = Path(args[i + 1])
        p.parent.mkdir(parents=True, exist_ok=True)
        p.write_text("# synthetic sibling tool output\\nsynthetic_fixture: true\\n")
        i += 2
        continue
    if a in OUTPUT_DIR_FLAGS and i + 1 < len(args):
        d = Path(args[i + 1])
        d.mkdir(parents=True, exist_ok=True)
        (d / "scaffold.t.sol").write_text("// synthetic scaffold\\n")
        (d / "RUN_INSTRUCTIONS.md").write_text("# synthetic run instructions\\n")
        i += 2
        continue
    i += 1

print("synthetic sibling tool stdout")
sys.exit({exit_code})
"""


def _write_mock_tool(tools_dir: Path, filename: str, exit_code: int) -> Path:
    p = tools_dir / filename
    p.write_text(MOCK_TOOL_TEMPLATE.format(exit_code=exit_code))
    p.chmod(0o755)
    return p


def _run_pipeline(
    workspace: Path,
    cluster: str,
    out_dir: Path,
    tools_override: Path,
    extra_args: List[str] = None,
    strict: bool = False,
) -> subprocess.CompletedProcess:
    cmd = [
        sys.executable,
        str(PIPELINE),
        "--workspace", str(workspace),
        "--cluster", cluster,
        "--target-platform", "cantina",
        "--target-protocol", "test-protocol",
        "--out-dir", str(out_dir),
    ]
    if strict:
        cmd.append("--strict")
    if extra_args:
        cmd.extend(extra_args)
    env = os.environ.copy()
    env["AUDITOOOR_WAVE3_TOOLS_DIR"] = str(tools_override)
    return subprocess.run(cmd, capture_output=True, text=True, env=env)


def _read_state(out_dir: Path) -> Dict:
    return json.loads((out_dir / "pipeline_state.json").read_text())


class Wave3E2EPipelineTests(unittest.TestCase):
    def setUp(self) -> None:
        self.tmp = Path(tempfile.mkdtemp(prefix="wave3-e2e-"))
        self.workspace = self.tmp / "ws"
        self.workspace.mkdir()
        self.out_dir = self.tmp / "out"
        self.tools_override = self.tmp / "tools_override"
        self.tools_override.mkdir()

    def tearDown(self) -> None:
        shutil.rmtree(self.tmp, ignore_errors=True)

    # 1. Full pipeline COMPLETE with all 4 mocks present + step 3 satisfied.
    def test_full_pipeline_complete(self) -> None:
        _write_mock_tool(self.tools_override, "wave3-cluster-to-hacker-brief.py", 0)
        _write_mock_tool(self.tools_override, "wave3-poc-scaffold-generator.py", 0)
        _write_mock_tool(self.tools_override, "wave3-published-source-originality-scanner.py", 0)
        _write_mock_tool(self.tools_override, "wave3-paste-ready-packager.py", 0)
        # The mocks for steps 4 + 5 require a finding draft. Pre-seed it so
        # the sibling subprocess does not need to invent one.
        self.out_dir.mkdir(parents=True, exist_ok=True)
        (self.out_dir / "04_finding_draft.md").write_text("# draft\nsynthetic_fixture: true\n")

        proc = _run_pipeline(self.workspace, "test-cluster", self.out_dir, self.tools_override)
        self.assertEqual(proc.returncode, 0, msg=proc.stderr)

        state = _read_state(self.out_dir)
        self.assertEqual(state["schema"], "auditooor.wave3_e2e_pipeline.v1")
        self.assertEqual(state["overall_status"], "COMPLETE")
        self.assertIn("Paste from", state["recommended_next_action"])
        self.assertEqual(state["steps_blocked"], [])
        # Steps 1, 2, 3, 4, 5 all OK.
        for step in (1, 2, 3, 4, 5):
            self.assertEqual(
                state["step_results"][str(step)]["status"],
                "OK",
                msg=f"step {step}: {state['step_results'][str(step)]}",
            )

    # 2. WAITING_FOR_OPERATOR after step 2 when scaffold is empty (no PoC).
    def test_waiting_for_operator(self) -> None:
        # Steps 1 + 2 land; step 3 finds the scaffold but no PoC test file
        # (we suppress the mock's scaffold output by skipping out-dir flag).
        _write_mock_tool(self.tools_override, "wave3-cluster-to-hacker-brief.py", 0)
        # A scaffold mock that intentionally does NOT create the RUN_INSTRUCTIONS
        # or a PoC test file - simulates a scaffold that left the operator
        # work to do.
        empty_scaffold = self.tools_override / "wave3-poc-scaffold-generator.py"
        empty_scaffold.write_text(
            "#!/usr/bin/env python3\n"
            "# synthetic_fixture: true\n"
            "import sys, os\n"
            "from pathlib import Path\n"
            "args = sys.argv[1:]\n"
            "for i,a in enumerate(args):\n"
            "    if a == '--out-dir' and i+1 < len(args):\n"
            "        Path(args[i+1]).mkdir(parents=True, exist_ok=True)\n"
            "print('scaffold-only')\n"
            "sys.exit(0)\n"
        )
        empty_scaffold.chmod(0o755)
        _write_mock_tool(self.tools_override, "wave3-published-source-originality-scanner.py", 0)
        _write_mock_tool(self.tools_override, "wave3-paste-ready-packager.py", 0)

        proc = _run_pipeline(self.workspace, "test-cluster", self.out_dir, self.tools_override)
        self.assertEqual(proc.returncode, 0, msg=proc.stderr)

        state = _read_state(self.out_dir)
        self.assertEqual(state["overall_status"], "WAITING_FOR_OPERATOR")
        self.assertIn("Build PoC at", state["recommended_next_action"])
        self.assertEqual(state["step_results"]["3"]["status"], "WAITING_FOR_OPERATOR")
        # Steps 4 and 5 must not have run.
        self.assertNotIn("4", state["step_results"])
        self.assertNotIn("5", state["step_results"])

    # 3. BLOCKED on originality scanner.
    def test_blocked_on_originality(self) -> None:
        _write_mock_tool(self.tools_override, "wave3-cluster-to-hacker-brief.py", 0)
        _write_mock_tool(self.tools_override, "wave3-poc-scaffold-generator.py", 0)
        _write_mock_tool(self.tools_override, "wave3-published-source-originality-scanner.py", 3)
