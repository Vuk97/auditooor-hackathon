# ---------------------------------------------------------------------------
# ARTIFACT shape
# ---------------------------------------------------------------------------
def test_artifact_shape(tmp_path):
    _scope(tmp_path, ["src/foo"])
    _mk(tmp_path, "src/foo/bar_test.go",
        'func TestReset(t *testing.T) { t.Skip("flaky") }\n')
    scan.analyze(tmp_path, write=True)
    art = tmp_path / ".auditooor" / "skipped_test_markers.jsonl"
    assert art.is_file()
    for line in art.read_text().splitlines():
        if not line.strip():
            continue
        row = json.loads(line)
        assert row["schema"] == "auditooor.skipped_test_marker.v1"
        assert row["disposition"] == "open"
        assert row["file"] and isinstance(row["line"], int)
    result = json.loads((tmp_path / ".auditooor" / "skipped_test_markers_result.json").read_text())
    assert "verdict" in result
    assert "n_markers" in result


