def test_attribution_resolved(tmp_path):
    _scope(tmp_path, ["src/foo"])
    _mk(tmp_path, "src/foo/batch_queue.go", "package foo\nfunc Reset() {}\n")
    _mk(tmp_path, "src/foo/batch_queue_test.go",
        'func TestBatchQueue_Reset(t *testing.T) { t.Skip("x") }\n')
    _manifest(tmp_path, [
        {"file": "src/foo/batch_queue.go", "function": "Reset",
         "file_line": "src/foo/batch_queue.go:2", "lang": "go"},
    ])
    res = scan.analyze(tmp_path)
    row = next(r for r in res["rows"] if r["test_name"] == "TestBatchQueue_Reset")
    assert row["attribution"] == "resolved"
    assert row["exercises_symbol"] is not None


