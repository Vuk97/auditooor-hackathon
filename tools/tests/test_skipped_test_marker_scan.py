def test_gate_disposed_via_rebuttal(tmp_path):
    _scope(tmp_path, ["src/foo"])
    p = _mk(tmp_path, "src/foo/bar_test.go",
            'func TestReset(t *testing.T) { t.Skip("flaky") }\n')
    scan.analyze(tmp_path, write=True)
    # find the row's line
    art = tmp_path / ".auditooor" / "skipped_test_markers.jsonl"
    row = json.loads(art.read_text().splitlines()[0])
    (tmp_path / ".auditooor" / "skipped_test_rebuttals.txt").write_text(
        f"rebut: {row['file']}:{row['line']}: known CI-only flake, behavior covered elsewhere\n",
        encoding="utf-8")
    res = gate.evaluate(tmp_path)
    assert res["verdict"] == "pass-skipped-tests-disposed"
    assert res["conformance_pass"] is True


def test_gate_disposed_via_hunt_sidecar(tmp_path):
    _scope(tmp_path, ["src/foo"])
    _mk(tmp_path, "src/foo/batch_queue.go", "package foo\nfunc Reset() {}\n")
    _mk(tmp_path, "src/foo/batch_queue_test.go",
        'func TestBatchQueue_Reset(t *testing.T) { t.Skip("x") }\n')
    _manifest(tmp_path, [
        {"file": "src/foo/batch_queue.go", "function": "Reset",
         "file_line": "src/foo/batch_queue.go:2", "lang": "go"},
    ])
    scan.analyze(tmp_path, write=True)
    # write a hunt sidecar over the exercised file
    sc = tmp_path / ".auditooor" / "hunt_findings_sidecars"
    sc.mkdir(parents=True, exist_ok=True)
    (sc / "h0.json").write_text(json.dumps({
        "schema": "auditooor.hunt_finding_sidecar.v1",
        "status": "negative",
        "function_anchor": {"file": str(tmp_path / "src/foo/batch_queue.go"), "fn": "Reset"},
        "file_line": "src/foo/batch_queue.go:2",
    }), encoding="utf-8")
    res = gate.evaluate(tmp_path)
    assert res["verdict"] == "pass-skipped-tests-disposed"
    assert res["conformance_pass"] is True


