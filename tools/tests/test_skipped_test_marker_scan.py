# OUT-OF-SCOPE (longest-prefix)
# ---------------------------------------------------------------------------
def test_out_of_scope_prefix(tmp_path):
    _scope(tmp_path, ["src/rust/op-reth"], ["src/rust/kona"])
    _mk(tmp_path, "src/rust/kona/x_test.go",
        'func TestKona(t *testing.T) { t.Skip("oos") }\n')
    _mk(tmp_path, "src/rust/op-reth/y_test.go",
        'func TestReth(t *testing.T) { t.Skip("inscope") }\n')
    res = scan.analyze(tmp_path)
