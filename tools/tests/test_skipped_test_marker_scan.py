"""Guard tests for skipped-test-marker-scan.py + skipped-test-disposition-gate.py.

Covers the spec's concrete cases incl. the NON-VACUOUS negative (.only is not a
skip; a file with no markers must yield zero rows), scaffold exclusion (the
chimera-scaffold/recon-log-bridge false-positive guard), OOS longest-prefix
exclusion, attribution, artifact shape, and the scope-unreadable error path.
"""
from __future__ import annotations

import importlib.util
import json
from pathlib import Path

import pytest

_TOOLS = Path(__file__).resolve().parent.parent


def _load(modname: str, filename: str):
    spec = importlib.util.spec_from_file_location(modname, str(_TOOLS / filename))
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return mod


scan = _load("_skipscan", "skipped-test-marker-scan.py")
gate = _load("_skipgate", "skipped-test-disposition-gate.py")


def _mk(ws: Path, rel: str, content: str):
    p = ws / rel
    p.parent.mkdir(parents=True, exist_ok=True)
    p.write_text(content, encoding="utf-8")
    return p


def _scope(ws: Path, in_scope, out_of_scope=None):
    (ws / "scope.json").write_text(
        json.dumps({"in_scope": in_scope, "out_of_scope": out_of_scope or []}),
        encoding="utf-8",
    )


def _manifest(ws: Path, rows):
    p = ws / ".auditooor" / "inscope_units.jsonl"
    p.parent.mkdir(parents=True, exist_ok=True)
    with p.open("w", encoding="utf-8") as fh:
        for r in rows:
            fh.write(json.dumps(r) + "\n")


# ---------------------------------------------------------------------------
# POSITIVE go
# ---------------------------------------------------------------------------
def test_positive_go(tmp_path):
    _scope(tmp_path, ["src/foo"])
    _mk(tmp_path, "src/foo/bar_test.go",
        'package foo\n\nfunc TestReset(t *testing.T) {\n\tt.Skip("flaky")\n}\n')
    res = scan.analyze(tmp_path)
    assert res["verdict"] == "found-skipped-tests"
    go_rows = [r for r in res["rows"] if r["lang"] == "go"]
    assert any(r["marker"] == "t.Skip" and r["test_name"] == "TestReset"
               and "flaky" in r["skip_reason"] for r in go_rows)
    art = tmp_path / ".auditooor" / "skipped_test_markers.jsonl"
    assert art.is_file()
    lines = [json.loads(l) for l in art.read_text().splitlines() if l.strip()]
    assert any(r["test_name"] == "TestReset" for r in lines)


