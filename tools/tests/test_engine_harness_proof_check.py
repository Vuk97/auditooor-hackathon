        # structural (skipped), Setup ignored.
        self.assertEqual(len(r["proven"]), 1, r)
        self.assertEqual(r["unproven"], [])
        joined = " ".join(r["proven"] + r["unproven"])
        self.assertIn("CryticToFoundry.sol", " ".join(r["proven"]))
        self.assertNotIn("CryticTester.sol", joined)
        self.assertNotIn("Properties.sol", joined)

