        spec.loader.exec_module(ac)
        loaded = ac._load_engine_proof_gate_module()
        self.assertIsNotNone(loaded, "audit-completeness must find engine-harness-proof-check.py")
        self.assertTrue(hasattr(loaded, "evaluate"))


