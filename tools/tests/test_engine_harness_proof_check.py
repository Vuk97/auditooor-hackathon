    def _blocked_timeout_manifest(self, ws, harness_dir, inv_status="timeout"):
        harness_dir.mkdir(parents=True, exist_ok=True)
        for name in ("Halmos_A", "Halmos_B", "Halmos_C"):
            (harness_dir / f"{name}.t.sol").write_text(PER_FUNCTION_SCAFFOLD)
        manifest = ws / ".audit_logs" / "solidity_per_function_halmos_manifest.json"
        manifest.parent.mkdir(parents=True, exist_ok=True)
        manifest.write_text(json.dumps({
            "schema": "auditooor.solidity_per_function_halmos.v1",
            "status": "blocked",
            "truncated_by_total_budget": True,
            "expected_invocation_count": 261,
            "executed_invocation_count": 3,
            "ok_invocation_count": 0,
            "skipped_invocation_count": 258,
            "invocations": [
                {"status": inv_status, "harness_contract": n,
                 "harness_path": str(harness_dir / f"{n}.t.sol")}
                for n in ("Halmos_A", "Halmos_B", "Halmos_C")
            ],
        }))

    def test_budget_blocked_timeout_halmos_is_advisory_with_real_harness(self):
        # halmos genuinely attempted but every invocation hit the per-harness timeout,
        # exhausting the total budget (the optimism 3/261-then-blocked shape). With a
        # genuine fuzz harness proven, the blocked-halmos arm must be ADVISORY, not a
        # veto. README STEP-4b: "attempt halmos (honest timeout caveat OK)".
        ws = self._ws_paths({"poc-tests/x-engine-harness/FuzzProps.sol": REAL_SOL})
        self._blocked_timeout_manifest(ws, ws / "poc-tests" / "per_function_invariants")
        r = self.mod.evaluate(ws)
        self.assertEqual(r["verdict"], "pass-engine-harness-proof")
        self.assertEqual(r["unproven"], [])
        adv = next(row for row in r["harnesses"] if row["harness"].startswith("solidity-per-function-halmos"))
        self.assertTrue(adv["advisory_only"])

    def test_budget_blocked_timeout_halmos_alone_still_fails_no_proof(self):
        # the same blocked-timeout manifest as the ONLY signal (no fuzz harness) must
        # NOT pass - an advisory caveat can never be the sole proof (no false-green).
        ws = self._ws_paths({})
        self._blocked_timeout_manifest(ws, ws / "poc-tests" / "per_function_invariants")
        r = self.mod.evaluate(ws)
        self.assertEqual(r["verdict"], "fail-no-proven-harness")

    def test_blocked_with_error_invocation_is_not_advisory(self):
        # a NON-timeout failure (error/build-failed) is a genuinely broken arm, not a
        # slow one - it must NOT be granted the timeout advisory.
        ws = self._ws_paths({"poc-tests/x-engine-harness/FuzzProps.sol": REAL_SOL})
        self._blocked_timeout_manifest(ws, ws / "poc-tests" / "per_function_invariants", inv_status="error")
        r = self.mod.evaluate(ws)
        adv = next(row for row in r["harnesses"] if row["harness"].startswith("solidity-per-function-halmos"))
        self.assertFalse(adv.get("advisory_only"))
        self.assertIn("solidity-per-function-halmos:0/261", r["unproven"])

