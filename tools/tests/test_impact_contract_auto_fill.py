"""Unit tests for tools/impact-contract-auto-fill.py.

Hermetic dry-run smoke test: synthesize a tiny workspace with one
skeleton, then run the tool with --dry-run and assert that:
  - 5 prompt files are emitted
  - the manifest matches schema_version
  - the lint subprocess is exec'd against agent-dispatch-prompt-lint.py
"""
from __future__ import annotations

import importlib.util
import json
import shutil
import subprocess
import sys
import tempfile
import textwrap
import unittest
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parent.parent.parent
TOOL = REPO_ROOT / "tools" / "impact-contract-auto-fill.py"
SCAFFOLDER = REPO_ROOT / "tools" / "impact-contract-scaffolder.py"
LINTER = REPO_ROOT / "tools" / "agent-dispatch-prompt-lint.py"


SAMPLE_SKELETON = textwrap.dedent(
    """\
    # Impact Contract Skeleton — TEST-AUTOFILL-01

    > Auto-generated by `tools/impact-contract-scaffolder.py` for the H-03 /
    > PR603 § Gate 2 unblock. This is a SKELETON — fill in every
    > `<TODO_OPERATOR>` marker before this row can drive `make harness-scaffold`.

    ## Identity

    | Field | Value |
    |---|---|
    | Row ID | `TEST-AUTOFILL-01` |
    | Harness family | `live_check` |
    | Source invariant family | `impact_node_liveness` |
    | Plan reason | Synthetic test fixture. |
    | Target entrypoint (from plan) | `<TODO_OPERATOR>` |
    | Status | `blocked_missing_impact_contract` |

    ## Title (T-02 schema)

    **Proposed title (PLACEHOLDER):**

    > <TODO_OPERATOR>_class in impact_node_liveness leads to <TODO_OPERATOR>_impact

    ## Severity / Likelihood / Impact

    - **Severity tier:** `<TODO_OPERATOR>` (Critical / High / Medium / Low)

    ## Test command

    - **Compile / run command (from plan):** `<TODO_OPERATOR>`
    - **Expected log string (from plan):** `<TODO_OPERATOR>`
    """
)


class ImpactContractAutoFillTests(unittest.TestCase):
    def setUp(self) -> None:
        self.tmp = Path(tempfile.mkdtemp(prefix="auditooor-autofill-test-"))
        self.ws = self.tmp / "ws"
        contracts_dir = self.ws / ".auditooor" / "impact_contracts"
        contracts_dir.mkdir(parents=True, exist_ok=True)
        (contracts_dir / "test-autofill-01.md").write_text(
            SAMPLE_SKELETON, encoding="utf-8"
        )
        # Also dump a non-skeleton md to confirm the discoverer ignores
        # _autofilled.md and INDEX.md
        (contracts_dir / "INDEX.md").write_text("# index", encoding="utf-8")
        (contracts_dir / "stale_autofilled.md").write_text(
            "stale", encoding="utf-8"
        )

    def tearDown(self) -> None:
        shutil.rmtree(self.tmp, ignore_errors=True)

    def test_dry_run_emits_prompts_and_manifest(self) -> None:
        proc = subprocess.run(
            [
                sys.executable,
                str(TOOL),
                "--workspace",
                str(self.ws),
                "--dry-run",
                "--print-json",
            ],
            capture_output=True,
            text=True,
            check=False,
        )
        self.assertEqual(proc.returncode, 0, proc.stdout + proc.stderr)
        manifest = json.loads(proc.stdout)
        self.assertEqual(
            manifest["schema_version"], "auditooor.impact_contract_auto_fill.v1"
        )
        self.assertEqual(manifest["selected_count"], 1)
        self.assertTrue(manifest["dry_run"])
        # 5 prompts emitted per row
        row = manifest["rows"][0]
        self.assertEqual(row["row_id"], "TEST-AUTOFILL-01")
        self.assertEqual(len(row["prompt_paths"]), 5)
        for p in row["prompt_paths"]:
            self.assertTrue(Path(p).is_file(), p)
        # No autofilled file written in dry-run
        autofilled = self.ws / ".auditooor" / "impact_contracts" / (
            "test-autofill-01_autofilled.md"
        )
        self.assertFalse(autofilled.is_file())

    def test_no_dry_run_without_budget_approval_refuses(self) -> None:
        proc = subprocess.run(
            [
                sys.executable,
                str(TOOL),
                "--workspace",
                str(self.ws),
                "--no-dry-run",
            ],
            capture_output=True,
            text=True,
            check=False,
        )
        self.assertEqual(proc.returncode, 2, proc.stdout + proc.stderr)
        self.assertIn("budget", proc.stderr.lower())

    def test_lint_strict_mode(self) -> None:
        # If lint --strict ever fails on emitted prompts, this surfaces it.
        proc = subprocess.run(
            [
                sys.executable,
                str(TOOL),
                "--workspace",
                str(self.ws),
                "--dry-run",
                "--lint-strict",
            ],
            capture_output=True,
            text=True,
            check=False,
        )
        self.assertEqual(proc.returncode, 0, proc.stdout + proc.stderr)


if __name__ == "__main__":
    unittest.main()
