class TestParseCargoLock(unittest.TestCase):
    def test_parses_git_source_sha(self):
        pkgs = mod._parse_cargo_lock_text(SAMPLE_CARGO_LOCK_WITH_GIT)
        self.assertIn("solana-sdk", pkgs)
        sdk_entries = pkgs["solana-sdk"]
        self.assertTrue(any("lock_sha" in e for e in sdk_entries))
        sdk = next(e for e in sdk_entries if "lock_sha" in e)
        self.assertEqual(sdk["lock_sha"], "a1b2c3d4e5f6a1b2c3d4e5f6a1b2c3d4e5f6a1b2")

    def test_parses_anchor_sha(self):
        pkgs = mod._parse_cargo_lock_text(SAMPLE_CARGO_LOCK_WITH_GIT)
        self.assertIn("anchor-lang", pkgs)
        anchor = next(e for e in pkgs["anchor-lang"] if "lock_sha" in e)
        self.assertEqual(anchor["lock_sha"], "deadbeefcafe1234deadbeefcafe1234deadbeef")

    def test_parses_rev_pinned_sha(self):
        pkgs = mod._parse_cargo_lock_text(SAMPLE_CARGO_LOCK_WITH_GIT)
        self.assertIn("test-kit", pkgs)
        kit = next(e for e in pkgs["test-kit"] if "lock_sha" in e)
        self.assertEqual(kit["lock_sha"], "abc123def456abc123def456abc123def456abc1")

    def test_registry_packages_have_no_lock_sha(self):
        pkgs = mod._parse_cargo_lock_text(SAMPLE_CARGO_LOCK_WITH_GIT)
        # serde is a registry dep, should not have lock_sha
        serde = pkgs.get("serde", [])
        self.assertTrue(all("lock_sha" not in e for e in serde))

    def test_no_git_deps_in_lock(self):
        pkgs = mod._parse_cargo_lock_text(SAMPLE_CARGO_LOCK_NO_GIT)
        # No packages should have lock_sha
        for entries in pkgs.values():
            for e in entries:
                self.assertNotIn("lock_sha", e)

    def test_missing_cargo_lock_returns_empty(self):
        with tempfile.TemporaryDirectory() as tmpdir:
            ws = pathlib.Path(tmpdir)
            pkgs = mod.parse_cargo_lock(ws)
            self.assertEqual(pkgs, {})


