class TestAnalyzeGitDepOffline(unittest.TestCase):
    def setUp(self):
        self._orig = mod.OFFLINE
        mod.OFFLINE = True

    def tearDown(self):
        mod.OFFLINE = self._orig

    def _make_ws(self, cargo_toml_text: str, cargo_lock_text: str = "") -> pathlib.Path:
        self._tmpdir = tempfile.mkdtemp()
        ws = pathlib.Path(self._tmpdir)
        (ws / "Cargo.toml").write_text(cargo_toml_text)
        if cargo_lock_text:
            (ws / "Cargo.lock").write_text(cargo_lock_text)
        return ws

    def tearDown(self):
        super().tearDown()
        import shutil
        if hasattr(self, "_tmpdir"):
            shutil.rmtree(self._tmpdir, ignore_errors=True)

    def test_lock_sha_resolved_from_cargo_lock(self):
        ws = self._make_ws(SAMPLE_CARGO_TOML_GIT, SAMPLE_CARGO_LOCK_WITH_GIT)
        lock_packages = mod.parse_cargo_lock(ws)
        deps = mod.parse_cargo_toml(ws)
        sdk_dep = next(d for d in deps if d["name"] == "solana-sdk")
        with tempfile.TemporaryDirectory() as tmpdir:
            result = mod.analyze_git_dep(
                sdk_dep,
                lock_packages,
                clone_root=pathlib.Path(tmpdir),
                skip_clone=True,
                audit_pin=None,
            )
        self.assertEqual(result["lock_sha"], "a1b2c3d4e5f6a1b2c3d4e5f6a1b2c3d4e5f6a1b2")
        # Offline → unknown divergence
        self.assertIn(result["divergence"], ("unknown", "forked", "same"))

    def test_audit_pin_lag_note_added(self):
        ws = self._make_ws(SAMPLE_CARGO_TOML_GIT, SAMPLE_CARGO_LOCK_WITH_GIT)
        lock_packages = mod.parse_cargo_lock(ws)
        deps = mod.parse_cargo_toml(ws)
        sdk_dep = next(d for d in deps if d["name"] == "solana-sdk")
        with tempfile.TemporaryDirectory() as tmpdir:
            result = mod.analyze_git_dep(
                sdk_dep,
                lock_packages,
                clone_root=pathlib.Path(tmpdir),
                skip_clone=True,
                audit_pin="deadbeef1234deadbeef1234deadbeef12345678",
            )
        self.assertIn("audit_pin_lag_note", result)
        self.assertIn("deadbeef1234", result["audit_pin_lag_note"])

    def test_no_lock_file_resolves_empty_sha(self):
        ws = self._make_ws(SAMPLE_CARGO_TOML_GIT)  # no Cargo.lock
        lock_packages = mod.parse_cargo_lock(ws)
        deps = mod.parse_cargo_toml(ws)
        sdk_dep = next(d for d in deps if d["name"] == "solana-sdk")
        with tempfile.TemporaryDirectory() as tmpdir:
            result = mod.analyze_git_dep(
                sdk_dep,
                lock_packages,
                clone_root=pathlib.Path(tmpdir),
                skip_clone=True,
                audit_pin=None,
            )
        self.assertEqual(result["lock_sha"], "")


