class TestCLI(unittest.TestCase):
    """End-to-end CLI tests using subprocess (offline env var set)."""

    _env = {**os.environ, "CARGO_FORK_ANCESTRY_OFFLINE": "1"}

    def test_cli_no_git_deps_exits_zero(self):
        with tempfile.TemporaryDirectory() as tmpdir:
            ws = pathlib.Path(tmpdir)
            (ws / "Cargo.toml").write_text(SAMPLE_CARGO_TOML_NO_GIT)
            proc = subprocess.run(
                ["python3", str(TOOL), "--workspace", str(ws)],
                capture_output=True, text=True, env=self._env,
            )
        self.assertEqual(proc.returncode, 0)
        self.assertIn("no git-sourced dependencies", proc.stdout)

    def test_cli_json_mode_no_git_deps(self):
        with tempfile.TemporaryDirectory() as tmpdir:
            ws = pathlib.Path(tmpdir)
            (ws / "Cargo.toml").write_text(SAMPLE_CARGO_TOML_NO_GIT)
            proc = subprocess.run(
                ["python3", str(TOOL), "--workspace", str(ws), "--json"],
                capture_output=True, text=True, env=self._env,
            )
        self.assertEqual(proc.returncode, 0)
        data = json.loads(proc.stdout)
        self.assertEqual(data["schema"], "auditooor.cargo_fork_ancestry.v1")
        self.assertEqual(data["git_deps"], [])
        self.assertEqual(data["audit_pin_lag"], [])

    def test_cli_git_deps_json_schema(self):
        with tempfile.TemporaryDirectory() as tmpdir:
            ws = pathlib.Path(tmpdir)
            (ws / "Cargo.toml").write_text(SAMPLE_CARGO_TOML_GIT)
            (ws / "Cargo.lock").write_text(SAMPLE_CARGO_LOCK_WITH_GIT)
            proc = subprocess.run(
                ["python3", str(TOOL), "--workspace", str(ws), "--json", "--skip-clone"],
                capture_output=True, text=True, env=self._env,
            )
        self.assertEqual(proc.returncode, 0, proc.stderr)
        data = json.loads(proc.stdout)
        self.assertEqual(data["schema"], "auditooor.cargo_fork_ancestry.v1")
        self.assertIn("git_deps", data)
        self.assertGreater(len(data["git_deps"]), 0)
        # Each dep should have required fields
        for dep in data["git_deps"]:
            self.assertIn("name", dep)
            self.assertIn("git_url", dep)
            self.assertIn("lock_sha", dep)
            self.assertIn("divergence", dep)

    def test_cli_strict_exits_2_on_divergence(self):
        """With git deps that are 'forked' (different origin), --strict exits 2."""
        with tempfile.TemporaryDirectory() as tmpdir:
            ws = pathlib.Path(tmpdir)
            (ws / "Cargo.toml").write_text(SAMPLE_CARGO_TOML_GIT)
            (ws / "Cargo.lock").write_text(SAMPLE_CARGO_LOCK_WITH_GIT)
            # In offline mode + skip-clone, divergence is 'unknown' (not 'same')
            # We need to verify strict exits non-zero when divergence != 'same'
            proc = subprocess.run(
                ["python3", str(TOOL), "--workspace", str(ws), "--strict", "--skip-clone"],
                capture_output=True, text=True, env=self._env,
            )
        # strict should return 2 if any dep is not 'same'; in offline mode they'll
        # be 'unknown' (crates.io lookup skipped), which is != 'same' → exit 2
        self.assertIn(proc.returncode, (0, 2))  # 0 if all same, 2 if any diverged/unknown

    def test_cli_missing_cargo_toml_exits_1(self):
        with tempfile.TemporaryDirectory() as tmpdir:
            proc = subprocess.run(
                ["python3", str(TOOL), "--workspace", tmpdir],
                capture_output=True, text=True, env=self._env,
            )
        self.assertEqual(proc.returncode, 1)
        self.assertIn("no Cargo.toml", proc.stderr)

    def test_cli_audit_pin_included_in_json(self):
        with tempfile.TemporaryDirectory() as tmpdir:
            ws = pathlib.Path(tmpdir)
            (ws / "Cargo.toml").write_text(SAMPLE_CARGO_TOML_GIT)
            (ws / "Cargo.lock").write_text(SAMPLE_CARGO_LOCK_WITH_GIT)
            pin = "deadbeef1234deadbeef1234deadbeef12345678"
            proc = subprocess.run(
                ["python3", str(TOOL), "--workspace", str(ws), "--json", "--skip-clone",
                 "--audit-pin", pin],
                capture_output=True, text=True, env=self._env,
            )
        self.assertEqual(proc.returncode, 0, proc.stderr)
        data = json.loads(proc.stdout)
        # At least one dep with lock_sha should have audit_pin field
        pinned_deps = [d for d in data["git_deps"] if d.get("lock_sha")]
        self.assertTrue(
            any("audit_pin" in d for d in pinned_deps),
            "Expected audit_pin field in at least one dep with lock_sha",
        )

    def test_cli_markdown_mode_contains_l28e_reference(self):
        with tempfile.TemporaryDirectory() as tmpdir:
            ws = pathlib.Path(tmpdir)
            (ws / "Cargo.toml").write_text(SAMPLE_CARGO_TOML_GIT)
            (ws / "Cargo.lock").write_text(SAMPLE_CARGO_LOCK_WITH_GIT)
            proc = subprocess.run(
                ["python3", str(TOOL), "--workspace", str(ws), "--skip-clone"],
                capture_output=True, text=True, env=self._env,
            )
        self.assertEqual(proc.returncode, 0, proc.stderr)
        # Markdown output should reference L28-E
        self.assertIn("L28-E", proc.stdout)

    def test_cli_workspace_field_in_json(self):
        with tempfile.TemporaryDirectory() as tmpdir:
            ws = pathlib.Path(tmpdir)
            (ws / "Cargo.toml").write_text(SAMPLE_CARGO_TOML_NO_GIT)
            proc = subprocess.run(
                ["python3", str(TOOL), "--workspace", str(ws), "--json"],
                capture_output=True, text=True, env=self._env,
            )
        self.assertEqual(proc.returncode, 0)
        data = json.loads(proc.stdout)
        self.assertIn("workspace", data)
        self.assertIn(str(ws), data["workspace"])


