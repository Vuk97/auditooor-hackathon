class TestParseCargoToml(unittest.TestCase):
    def test_parses_git_deps(self):
        deps = mod._parse_cargo_toml_text(SAMPLE_CARGO_TOML_GIT)
        names = [d["name"] for d in deps]
        self.assertIn("solana-sdk", names)
        self.assertIn("anchor-lang", names)

    def test_detects_branch(self):
        deps = mod._parse_cargo_toml_text(SAMPLE_CARGO_TOML_GIT)
        sdk = next(d for d in deps if d["name"] == "solana-sdk")
        self.assertEqual(sdk["git_url"], "https://github.com/target-org/solana")
        self.assertEqual(sdk.get("branch"), "dev")

    def test_detects_tag(self):
        deps = mod._parse_cargo_toml_text(SAMPLE_CARGO_TOML_GIT)
        anchor = next(d for d in deps if d["name"] == "anchor-lang")
        self.assertEqual(anchor.get("tag"), "v0.28.0")

    def test_detects_rev_in_dev_dependencies(self):
        deps = mod._parse_cargo_toml_text(SAMPLE_CARGO_TOML_GIT)
        kit = next((d for d in deps if d["name"] == "test-kit"), None)
        self.assertIsNotNone(kit, "test-kit (dev-dep with rev) should be found")
        self.assertIn("rev", kit)

    def test_no_git_deps_returns_empty(self):
        deps = mod._parse_cargo_toml_text(SAMPLE_CARGO_TOML_NO_GIT)
        self.assertEqual(deps, [])

    def test_patch_section_parsed(self):
        deps = mod._parse_cargo_toml_text(SAMPLE_CARGO_TOML_PATCH)
        names = [d["name"] for d in deps]
        self.assertIn("solana-sdk", names)
        patched = next(d for d in deps if d["name"] == "solana-sdk")
        self.assertIn("fork-org", patched["git_url"])

    def test_filesystem_parse_from_workspace(self):
        with tempfile.TemporaryDirectory() as tmpdir:
            ws = pathlib.Path(tmpdir)
            (ws / "Cargo.toml").write_text(SAMPLE_CARGO_TOML_GIT)
            deps = mod.parse_cargo_toml(ws)
            names = [d["name"] for d in deps]
            self.assertIn("solana-sdk", names)

    def test_missing_cargo_toml_raises(self):
        with tempfile.TemporaryDirectory() as tmpdir:
            ws = pathlib.Path(tmpdir)
            with self.assertRaises(Exception):
                mod.parse_cargo_toml(ws)


