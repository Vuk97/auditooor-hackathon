class TestGitSourceRegex(unittest.TestCase):
    def test_branch_source(self):
        src = "git+https://github.com/org/repo?branch=main#a" * 1
        src = "git+https://github.com/org/repo?branch=main#" + "a" * 40
        m = mod.CARGO_LOCK_GIT_SOURCE_RE.match(src)
        self.assertIsNotNone(m)
        self.assertEqual(m.group("url"), "https://github.com/org/repo")
        self.assertEqual(m.group("sha"), "a" * 40)

    def test_tag_source(self):
        sha = "deadbeef" * 5
        src = f"git+https://github.com/org/repo?tag=v1.0.0#{sha}"
        m = mod.CARGO_LOCK_GIT_SOURCE_RE.match(src)
        self.assertIsNotNone(m)
        self.assertEqual(m.group("sha"), sha)

    def test_rev_source(self):
        sha = "1234cafe" * 5
        src = f"git+https://github.com/org/repo?rev={sha}#{sha}"
        m = mod.CARGO_LOCK_GIT_SOURCE_RE.match(src)
        self.assertIsNotNone(m)
        self.assertEqual(m.group("sha"), sha)

    def test_bare_git_source(self):
        sha = "abcdef01" * 5
        src = f"git+https://github.com/org/repo#{sha}"
        m = mod.CARGO_LOCK_GIT_SOURCE_RE.match(src)
        self.assertIsNotNone(m)
        self.assertEqual(m.group("sha"), sha)

    def test_registry_source_not_matched(self):
        src = "registry+https://github.com/rust-lang/crates.io-index"
        m = mod.CARGO_LOCK_GIT_SOURCE_RE.match(src)
        self.assertIsNone(m)


