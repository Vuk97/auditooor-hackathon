#!/usr/bin/env python3
"""Tests for tools/llm-pr-review-merge-hook.py — auto-grade-on-merge.

Hermetic: no live `gh`, no real ledger writes. The ledger path is redirected
to a tmp file in every test; subprocess.run for `gh` is mocked. Test inputs
are NEUTRAL (paraphrased) PR comment bodies — no real PR comment text reused
verbatim, to avoid comment-leakage in fixtures (foot-gun #2).
"""
from __future__ import annotations

import importlib.util
import json
import pathlib
import tempfile
import unittest
from unittest.mock import patch

ROOT = pathlib.Path(__file__).resolve().parents[2]
TOOL = ROOT / "tools" / "llm-pr-review-merge-hook.py"


def _load_module():
    """Import the hyphenated tool as a module."""
    spec = importlib.util.spec_from_file_location("llm_pr_review_merge_hook", TOOL)
    assert spec is not None and spec.loader is not None
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return mod


