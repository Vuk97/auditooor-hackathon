# 3. find_review_comment — multi-comment threads
# ---------------------------------------------------------------------------

class FindReviewCommentTest(unittest.TestCase):
    def setUp(self) -> None:
        self.mod = _load_module()

    def test_picks_review_comment_from_thread(self) -> None:
        comments = [
            {"createdAt": "2026-04-25T10:00:00Z",
             "body": "looks great LGTM"},
            {"createdAt": "2026-04-25T10:05:00Z",
             "body": NEW_AGREED_MERGE},
            {"createdAt": "2026-04-25T10:10:00Z",
             "body": "bumping for visibility"},
        ]
        c = self.mod.find_review_comment(comments)
        self.assertIsNotNone(c)
        self.assertIn("Dual-LLM", c["body"])

    def test_picks_earliest_review_when_multiple(self) -> None:
        # If the dispatcher ran twice, prefer the earliest.
        comments = [
            {"createdAt": "2026-04-25T11:00:00Z",
             "body": NEW_AGREED_NEEDS_FIX},
            {"createdAt": "2026-04-25T10:00:00Z",
             "body": NEW_AGREED_MERGE},
        ]
        c = self.mod.find_review_comment(comments)
        self.assertIn("AGREED-MERGE-OK", c["body"])

    def test_no_review_comment_returns_none(self) -> None:
        comments = [{"body": "nice work"}, {"body": "+1"}]
        self.assertIsNone(self.mod.find_review_comment(comments))


