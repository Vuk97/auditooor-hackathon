# 1. parse_review_comment — both formats, both consensus shapes
# ---------------------------------------------------------------------------

class ParseReviewCommentTest(unittest.TestCase):
    def setUp(self) -> None:
        self.mod = _load_module()

    def test_legacy_agreed_merge_parses(self) -> None:
        p = self.mod.parse_review_comment(LEGACY_AGREED_MERGE)
        self.assertEqual(p["kimi"], "MERGE-OK")
        self.assertEqual(p["minimax"], "MERGE-OK")
        # Legacy "AGREED-MERGE" gets normalised to "AGREED-MERGE-OK".
        self.assertEqual(p["consensus"], "AGREED-MERGE-OK")

    def test_new_agreed_merge_parses(self) -> None:
        p = self.mod.parse_review_comment(NEW_AGREED_MERGE)
        self.assertEqual(p["kimi"], "MERGE-OK")
        self.assertEqual(p["minimax"], "MERGE-OK")
        self.assertEqual(p["consensus"], "AGREED-MERGE-OK")

    def test_new_agreed_needs_fix_parses(self) -> None:
        p = self.mod.parse_review_comment(NEW_AGREED_NEEDS_FIX)
        self.assertEqual(p["kimi"], "NEEDS-FIX")
        self.assertEqual(p["minimax"], "NEEDS-FIX")
        self.assertEqual(p["consensus"], "AGREED-NEEDS-FIX")

    def test_legacy_disagreed_parses(self) -> None:
        p = self.mod.parse_review_comment(LEGACY_DISAGREED)
        self.assertEqual(p["kimi"], "NEEDS-FIX")
        self.assertEqual(p["minimax"], "MERGE-OK")
        self.assertEqual(p["consensus"], "DISAGREED")

    def test_unknown_body_returns_nones(self) -> None:
        p = self.mod.parse_review_comment("just a normal comment, no markers")
        self.assertIsNone(p["kimi"])
        self.assertIsNone(p["minimax"])
        self.assertIsNone(p["consensus"])


