# 5. detect_fix_up — heuristic behaviour
# ---------------------------------------------------------------------------

class DetectFixUpTest(unittest.TestCase):
    def setUp(self) -> None:
        self.mod = _load_module()

    def test_commit_after_review_returns_true(self) -> None:
        pr = {
            "commits": [
                {"committedDate": "2026-04-25T09:00:00Z"},
                {"committedDate": "2026-04-25T12:30:00Z"},
            ],
        }
        review = {"createdAt": "2026-04-25T10:00:00Z"}
        self.assertTrue(self.mod.detect_fix_up(pr, review))

    def test_no_commits_after_review_returns_false(self) -> None:
        pr = {
            "commits": [{"committedDate": "2026-04-25T09:00:00Z"}],
        }
        review = {"createdAt": "2026-04-25T10:00:00Z"}
        self.assertFalse(self.mod.detect_fix_up(pr, review))

    def test_missing_review_timestamp_returns_false(self) -> None:
        pr = {"commits": [{"committedDate": "2026-04-25T12:00:00Z"}]}
        review = {}
        self.assertFalse(self.mod.detect_fix_up(pr, review))


