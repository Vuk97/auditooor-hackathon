# 2. grade_outcome — all four consensus paths
# ---------------------------------------------------------------------------

class GradeOutcomeTest(unittest.TestCase):
    def setUp(self) -> None:
        self.mod = _load_module()

    def test_agreed_merge_ok_merged_both_true(self) -> None:
        parsed = {"kimi": "MERGE-OK", "minimax": "MERGE-OK",
                  "consensus": "AGREED-MERGE-OK"}
        out = self.mod.grade_outcome(parsed, "MERGED")
        self.assertEqual(out, {"kimi": "TRUE", "minimax": "TRUE"})

    def test_agreed_needs_fix_merged_both_true(self) -> None:
        # Both flagged correctly + the fix shipped.
        parsed = {"kimi": "NEEDS-FIX", "minimax": "NEEDS-FIX",
                  "consensus": "AGREED-NEEDS-FIX"}
        out = self.mod.grade_outcome(parsed, "MERGED")
        self.assertEqual(out, {"kimi": "TRUE", "minimax": "TRUE"})

    def test_disagreed_merged_as_is_merge_ok_voter_true(self) -> None:
        parsed = {"kimi": "NEEDS-FIX", "minimax": "MERGE-OK",
                  "consensus": "DISAGREED"}
        out = self.mod.grade_outcome(parsed, "MERGED",
                                     fix_up_after_review=False)
        self.assertEqual(out, {"kimi": "FALSE", "minimax": "TRUE"})

    def test_disagreed_merged_after_fix_needs_fix_voter_true(self) -> None:
        parsed = {"kimi": "NEEDS-FIX", "minimax": "MERGE-OK",
                  "consensus": "DISAGREED"}
        out = self.mod.grade_outcome(parsed, "MERGED",
                                     fix_up_after_review=True)
        self.assertEqual(out, {"kimi": "TRUE", "minimax": "FALSE"})

    def test_closed_unmerged_both_indeterminate(self) -> None:
        parsed = {"kimi": "MERGE-OK", "minimax": "MERGE-OK",
                  "consensus": "AGREED-MERGE-OK"}
        out = self.mod.grade_outcome(parsed, "CLOSED")
        self.assertEqual(out, {"kimi": "INDETERMINATE",
                               "minimax": "INDETERMINATE"})

    def test_open_pr_both_indeterminate(self) -> None:
        parsed = {"kimi": "NEEDS-FIX", "minimax": "MERGE-OK",
                  "consensus": "DISAGREED"}
        out = self.mod.grade_outcome(parsed, "OPEN")
        self.assertEqual(out, {"kimi": "INDETERMINATE",
                               "minimax": "INDETERMINATE"})

    def test_llm_failure_consensus_indeterminate(self) -> None:
        parsed = {"kimi": None, "minimax": "MERGE-OK",
                  "consensus": "LLM-FAILURE"}
        out = self.mod.grade_outcome(parsed, "MERGED")
        self.assertEqual(out, {"kimi": "INDETERMINATE",
                               "minimax": "INDETERMINATE"})


