# 4. process_pr — ledger write side effect (mocked gh)
# ---------------------------------------------------------------------------

class ProcessPrLedgerWriteTest(unittest.TestCase):
    def setUp(self) -> None:
        self.mod = _load_module()
        self.tmp = tempfile.NamedTemporaryFile(
            mode="w", suffix=".jsonl", delete=False
        )
        self.tmp.close()
        self.ledger = pathlib.Path(self.tmp.name)
        # Start with an empty file.
        self.ledger.write_text("", encoding="utf-8")

    def tearDown(self) -> None:
        try:
            self.ledger.unlink()
        except OSError:
            pass

    def _read_ledger(self) -> list[dict]:
        rows = []
        for line in self.ledger.read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if line:
                rows.append(json.loads(line))
        return rows

    def test_agreed_merge_writes_two_true_rows(self) -> None:
        pr_data = {
            "number": 9001,
            "state": "MERGED",
            "mergedAt": "2026-04-25T12:00:00Z",
            "comments": [{
                "createdAt": "2026-04-25T11:00:00Z",
                "body": NEW_AGREED_MERGE,
            }],
            "commits": [{"committedDate": "2026-04-25T10:00:00Z"}],
        }
        with patch.object(self.mod, "fetch_pr", return_value=pr_data):
            rec = self.mod.process_pr(
                9001,
                ledger_path=self.ledger,
                dry_run=False,
                operator="test",
                session_id="test-session",
            )
        rows = self._read_ledger()
        self.assertEqual(len(rows), 2)
        providers = sorted(r["provider"] for r in rows)
        self.assertEqual(providers, ["kimi", "minimax"])
        for r in rows:
            self.assertEqual(r["task_type"], "pr-review")
            self.assertEqual(r["task_ref"], "PR #9001")
            self.assertEqual(r["verdict"], "TRUE")
            self.assertEqual(r["operator"], "test")
        self.assertEqual(rec["outcomes"],
                         {"kimi": "TRUE", "minimax": "TRUE"})

    def test_disagreed_merged_unchanged_writes_one_true_one_false(self) -> None:
        # No commits AFTER the review timestamp -> merged-as-is.
        pr_data = {
            "number": 9002,
            "state": "MERGED",
            "comments": [{
                "createdAt": "2026-04-25T11:00:00Z",
                "body": LEGACY_DISAGREED,
            }],
            "commits": [{"committedDate": "2026-04-25T10:00:00Z"}],
        }
        with patch.object(self.mod, "fetch_pr", return_value=pr_data):
            self.mod.process_pr(
                9002, ledger_path=self.ledger, dry_run=False,
                operator="test", session_id="test-session",
            )
        rows = self._read_ledger()
        verdicts = {r["provider"]: r["verdict"] for r in rows}
        # Kimi voted NEEDS-FIX -> merged unchanged means Kimi was wrong.
        self.assertEqual(verdicts["kimi"], "FALSE")
        # Minimax voted MERGE-OK -> aligns with merge state.
        self.assertEqual(verdicts["minimax"], "TRUE")

    def test_idempotent_skips_existing(self) -> None:
        # Pre-populate the ledger with a row for kimi PR #9003.
        self.mod._load_calibration_module()  # cache module
        cal = self.mod._load_calibration_module()
        cal.append_entry({
            "ts": "2026-04-25T10:00:00Z",
            "provider": "kimi",
            "task_type": "pr-review",
            "task_ref": "PR #9003",
            "verdict": "TRUE",
            "evidence": "preexisting",
            "operator": "manual",
            "session_id": "prior",
        }, path=self.ledger)
        pr_data = {
            "number": 9003,
            "state": "MERGED",
            "comments": [{
                "createdAt": "2026-04-25T11:00:00Z",
                "body": NEW_AGREED_MERGE,
            }],
            "commits": [],
        }
        with patch.object(self.mod, "fetch_pr", return_value=pr_data):
            rec = self.mod.process_pr(
                9003, ledger_path=self.ledger, dry_run=False,
                operator="test", session_id="test-session",
            )
        rows = self._read_ledger()
        # Still 2 rows total: the preexisting kimi + the new minimax.
        self.assertEqual(len(rows), 2)
        kimi_rows = [r for r in rows if r["provider"] == "kimi"]
        self.assertEqual(len(kimi_rows), 1)
        self.assertEqual(kimi_rows[0]["operator"], "manual")  # untouched
        # The skipped reason was reported.
        self.assertIn("kimi-already-logged", rec["skipped"])

    def test_dry_run_does_not_write(self) -> None:
        pr_data = {
            "number": 9004,
            "state": "MERGED",
            "comments": [{
                "createdAt": "2026-04-25T11:00:00Z",
                "body": NEW_AGREED_MERGE,
            }],
            "commits": [],
        }
        with patch.object(self.mod, "fetch_pr", return_value=pr_data):
            rec = self.mod.process_pr(
                9004, ledger_path=self.ledger, dry_run=True,
                operator="test", session_id="test-session",
            )
        # Ledger is empty.
        self.assertEqual(self._read_ledger(), [])
        # But the record reports the would-be entries.
        self.assertIsNotNone(rec["logged"]["kimi"])
        self.assertIsNotNone(rec["logged"]["minimax"])


